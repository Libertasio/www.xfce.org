<b>12.07.2003</b>

<p><b>Xfce 4.0-RC1</b> wurde ver&ouml;ffentlicht!</p>
<p>Das Xfce Projekt freut sich, den ersten Release Candidate der Xfce 4.0 Desktopumgebung und Entwicklungsbasis ver&ouml;ffentlichen zu k&ouml;nnen.</p>
<p>Xfce 4.0 ist eine neue Desktopumgebung, basierend auf GTK2, f&uuml;r unixartige Betriebssysteme wie z. B.  
Linux, FreeBSD, NetBSD und Solaris.</p>

<b>Xfce 4.0 ist:</b>
<ul>
<li>Modular: w&auml;hlen Sie aus den vorhandenen Paketen aus.</li>
<li>Modern und kompatibel: es h&auml;lt sich an Freedesktop-Standards.</li>
<li>Einfach zu benutzen und zu konfigurieren: Passen Sie Ihre Umgebung aus einem zentralen Konfigurationssystem an.</li>
<li>Grafisch ansprechend: es benutzt GTK2 Themen und stellt verschiedene Themen f&uuml;r Icons und Fensterdekorationen bereit.</li>
<li>Klein und gut: Es spart Systemressourcen f&uuml;r die Programme.</li>

<li>Komplett: Es besteht aus einem Panel, einem Fenstermanager, einer Taskleiste, einem Dateimanager  
  mit der M&ouml;glichkeit SMB-Netze zu durchsuchen, einem Druckerdialog, einer Desktophintergrund-Verwaltung, 
  verschiedenen n&uuml;tzlichen Plugins, und vielem mehr...</li>
</ul>

