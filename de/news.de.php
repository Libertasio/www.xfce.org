the window frame</li>
<li>native multihead support</li>
<li>several new themes</li>
<li>new themes location</li>
<li>supports the "show desktop" protocol</li>
<li>supports the KDE context help</li>
<li>Always on top option</li>
<li>icons in the "Alt+Tab" dialog</li>
<li>keyboard shorcuts editor included in the xfwm settings manager dialog</li>
<li>New xfwm4 theme location, changed from themes/xfwm4/<i>name</i> to themes/<i>name</i>/xfwm4/</li>
</ul>

<p><b>xfce-mcs-manager settings manager</b></p>

<ul class="submenu">
