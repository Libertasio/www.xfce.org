<b>2004/10/30 - Xfce 4.2 BETA2 is out</b>

<p>Today, the Xfce project is proud to announce the release of version <b>4.2-beta2</b> of its Desktop Environment and development framework. Xfce is designed for Unix platforms using the GTK+ toolkit, and follows the Freedesktop specifications. It can be downloaded from <a href="index.php?page=download&lang=de">here</a>.</p>

<p>Xfce 4.2-beta2 embodies the traditional UNIX philosophy of modularity and re-usability: you can install/launch each module/application separately if you do not need them all or have very limited system resources.</p>

<p>Xfce 4.2-beta2 is the first desktop environment to ship with an easy to use <b>graphical installation wizard</b>, which takes care of compiling and installing Xfce and contributed packages on your computer. See <a href="http://xfce-installers.os-cillation.com/">here</a> for download links and instructions.</p>

<p>This second beta version offers several new and awaited features in comparison with the previous 4.0 stable release, while continuing to be lightweight, including a brand new session manager, keyboard shortcut and desktop menu graphical editors, multihead support (Xinerama and real multiscreen), "kiosk mode" support, optional support for icons in the desktop menu, a desktop menu plugin for the panel, CUPS and BSD-LPR printing support, and a new icon theme.  Moreover, Xfce 4.2 provides "cutting edge" features, such as a compositing manager for xfwm4 (a manager for the new Xorg compositor extension, which brings true transparency to the Xfce desktop), as well as support for the base directory specification.  Xfce is now available in more than 25 languages.</p>

<p>We hope that you will give this release a try. Please report any bugs or issues with this release on the <a href="http://bugzilla.xfce.org/"><b>Xfce bug tracker</b></a> or on the <a href="http://lunar-linux.org/mailman/listinfo/xfce4-dev">Xfce development list</a>.</p>

<p>It includes, among others, the following applications :</p>

<ul class="submenu">

<li>xfwm4 : The Xfce 4.2 window manager.</li>
<li>xfce4-panel : The Xfce 4.2 panel. It provides easy-to-setup detachable menus and application launchers. It is compatible with the numerous plug-ins provided by the <a href="http://xfce-goodies.berlios.de/">Xfce Goodies Project</a>, including the popular taskbar plugin.</li>
<li>xfdesktop : The desktop background and root window menu manager.</li>
<li>xfce4-session : The session manager, which allows you to restore your desktop the way you left it.</li>
<li>xffm : A fast file manager, with double pane and integrated samba network browser.</li>
<li>xfce-utils : Provides the Xfce user guide, and, among other tools, xftaskbar4, a separate taskbar that provides an intuitive way to manage windows.</li>
<li>xfce4-appfinder : A new application finder, which allows you to search, launch, and find information about applications installed on your system.</li>
<li>xfce4-iconbox : An icon box that can be used as an alternative to the taskbar.</li>
<li>xfce-mcs-manager and xfce-mcs-plugins: Provides a centralized Multi-Channel Settings manager and plugins, which allow instant application of modified settings.</li>
<li>xfprint : The print dialog and manager.</li>

</ul>

<p>Xfce 4.2-beta2 includes its own GTK2 theme engine and window manager themes, bringing homogeneity to both business and personal desktops.</p>

<p><i>About the Xfce Project :</i> The Xfce Project is a collaboration of international developers working together to develop a desktop environment that is both lightweight and full featured.  Keeping with the Unix philosophy of small, modular programs that are very good at doing one thing, the Xfce Project has created a desktop environment which consists of lightweight components which can be strung together in virtually any configuration to create a desktop solution that fits any user just right.</p>

