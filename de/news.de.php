Anregungen an <a href="mailto:xfce4-dev@xfce.org">xfce4-dev at xfce.org</a> (Sie k&ouml;nnen die Liste <a href="http://lunar-linux.org/mailman/listinfo/xfce4-dev">hier</a> abbonieren). Sie helfen uns damit die bestm&ouml;gliche Version zu ver&ouml;ffentlichen.</p>

