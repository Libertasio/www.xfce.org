<h3>Utilidades y aplicaciones recomendadas :</h3>

<ul class="submenu">
<li><a href="http://rox.sourceforge.net/phpwiki/index.php/ROX-Filer">gestor de archivos ROX</a> : 
  un ligero pero potente gestor de archivos, que permite colocar iconos lanzadores de aplicaciones en el escritorio e iconos minimizados.</li>
<li><a href="http://www.os-cillation.com/article.php?sid=40">Terminal</a> : 	
a modern terminal emulator for the Unix/Linux desktop - primarly for the Xfce 
desktop environment.</li>
<li><a href="http://www.burtonini.com/blog/computers/devilspie">Devil's Pie</a> : una herramienta manipuladora de ventanas. Devil's Pie puede configurarse,
por ejemplo, para que una ventana aparezca en todos los escritorios virtuales, o para que no aparecezca en la barra de herramientas.</li>
<li><a href="http://hocwp.free.fr/xbindkeys/xbindkeys.html">XBindKeys</a> : un programa que le permite lanzar programas de la interfaz de comandos desde su teclado 
  o desde su ratón bajo las X.</li>
</ul>

<h3>Varios enlaces :</h3>

<ul class="submenu">

<li><a href="http://catb.org/~esr/faqs/smart-questions.html">How To Ask Questions The Smart Way</a></li>
<li><a href="http://www.gtk.org/">GTK+</a> : GTK+ es una herramienta multiplataforma para la creación de interfaces gráficas de usuario. </li>
<li><a href="http://www.freedesktop.org">Freedesktop.org</a> : un proyecto libre para trabajar en interoperabilidad
y tecnología compartida para entornos de escritorio para el sistema de ventanas X.</li>

<li><a href="http://www.lunar-linux.org/">Lunar-linux</a> : una distribución de Linux basada en código fuente, con un sistema
de empaquetado único.</li>

	<li><a href="http://www.xfld.org/">Xfld</a> : Xfld es un cd live demo del Xfce', 
	un sistema GNU/Linux (derivado de Knoppix) que puede ejecutarse completamente desde 
	CD. Lleva una versión cvs actualizada del entorno de escritorio Xfce 4.1.</li>
	
	<li><a href="http://www.os-cillation.com/">os-cillation</a> : os-cillation e.K. 
	es una compañía especializada en desarrollo de software, consulting y de planificación
	de proyectos, así como soluciones en servidor, y que está localizada en Siegen, Alemania. 
	Además, da suporte comercial para Xfce, y proporciona 
	<a href="http://www.os-cillation.com/article.php?sid=37">paquetes para Debian</a> 
	de la versión cvs de Xfce 4.1</li>

<li><a href="http://cobind.com/index.html">	Cobind</a> : "Based on Fedora Core Linux, Cobind Desktop marries Xfce and Nautilus into a cohesive desktop experience"</li>
	
	<li><a href="http://spblinux.sourceforge.net/">SPB-Linux</a> : a small, stable, 
	up to date linux mini distribution which runs completely in ramdisk, can be booted 
	from a usb storage media, and now runs Xfce 4.2
	</li>
	
<li><a href="http://bluefish.openoffice.nl/index.html">Bluefish</a> : un potente editor para experimentados 
diseñadores y programadores web.</li>


<li><a href="http://www.themedepot.org/">Themedepot.org</a> : proporciona una selección de temas.</li>

<li><a href="http://www.freesbie.org/">FreeSBIE</a> : un sistema basado en FreeBSD, ejecutable directamente del CD, que ahora incluye el nuevo entorno de escritorio Xfce 4.</li>
<li><a href="http://www.zenwalk.org">ZenWalk</a>:  ZenWalk Linux distribution includes Xfce 4.2 as its default and unique Desktop Environment. It aims to be focused on Internet application, multimedia and coding tools.</li>
</ul>

