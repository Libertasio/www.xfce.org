<p>Hay una serie de paquetes adicionales que no son partes del lanzamiento
principal que proporcionan temas o funcionalidad extra. Algunos de ellos son partes del lanzamiento oficial de XFce 4, otros están bajo desarrollo.</p>

<a name="themes"></a>

<h3>Temas</h3>

<ul class="submenu">

<li><b>xfwm4-themes : </b>Decoración de ventanas para xwfm4. Si quiere previsualizar todos los temas, pinche <a href="index.php?page=xfwm4_themes&lang=es">aquí</a> (es un página <b>pesada</b>).</li>

