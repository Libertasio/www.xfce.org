<h3>Рекомендуемые приложения и утилиты:</h3>

<ul class="submenu">
	
	<li><a href="http://rox.sourceforge.net/phpwiki/index.php/ROX-Filer">
	ROX filer</a>: компактный, но мощный файловый менеджер, позволяющий создавать ссылки на приложения на рабочем столе и значки свернутых приложений.</li>
	
	<li><a href="http://www.os-cillation.com/article.php?sid=40">Terminal</a> : 
	a modern terminal emulator for the Unix/Linux desktop - primarly for the 
	Xfce desktop environment.</li>
	
	<li><a href="http://www.burtonini.com/blog/computers/devilspie">
	Devil's Pie</a>: утилита управления окнами. С её помощью можно сделать так, чтобы некоторое окно отображалось на всех рабочих местах, или не появлялось в панели задач.</li>
	
	<li><a href="http://hocwp.free.fr/xbindkeys/xbindkeys.html">
	XBindKeys</a>: программа, позволяющая выполнять консольные команды при помощи сочетаний клавиш или мыши в X Window.</li>
	
</ul>

