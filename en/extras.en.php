<p>There are a number of additional packages that are not part of 
the main desktop that provide themes or extra functionality. Some 
of them are part of the official XFce 4 release, others are still under 
development.</p>

<a name="themes"></a>

<h3>Themes</h3>

<ul class="submenu">
	<li><b>xfwm4-themes : </b>Window decoration themes for xfwm4. If you want to preview all xfwm 
	4 themes, click <a href="index.php?page=xfwm4_themes&lang=en">here</a> (<b>heavy</b> page).</li>
	<li><b>xffm-icons : </b>Icon themes for the xffm file manager.</li>
	<li><b>xfce4-themes : </b>Icon themes for the XFce 4 panel.</li>
</ul>

