<h3>Recommended applications and utilities :</h3>

<ul class="submenu">
	
	<li><a href="http://rox.sourceforge.net/phpwiki/index.php/ROX-Filer">
	ROX filer</a> : a lightweight but powerful file manager, that allows launchers 
	on the desktop and icons for minimized applications.</li>
	
	<li><a href="http://www.os-cillation.com/article.php?sid=40">Terminal</a> : 
	a modern terminal emulator for the Unix/Linux desktop - primarly for the 
	Xfce desktop environment.</li>
	
	<li><a href="http://www.burtonini.com/blog/computers/devilspie">
	Devil's Pie</a> : a window manipulation tool. Devil's Pie can be configured, 
	for example,  to make a window appear on all workspaces, or not appear in the 
	taskbar.</li>
	
	<li><a href="http://hocwp.free.fr/xbindkeys/xbindkeys.html">
	XBindKeys</a> : a program that allows you to launch shell commands with your 
	keyboard or your mouse under X Window.</li>
	
</ul>

