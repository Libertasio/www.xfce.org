<h3>Applicazioni raccomandate e utilità :</h3>

<ul class="submenu">
	
	<li><a href="http://rox.sourceforge.net/phpwiki/index.php/ROX-Filer">
	ROX filer</a> : un file manager leggero ma potente, fornisce launchers 
	nel desktop e icone per applicazioni minimizzate.</li>
	
	<li><a href="http://www.os-cillation.com/article.php?sid=40">Terminal</a> : 
	un moderno emulatore di terminale per i desktop Unix/Linux - principalmente per 
	l'Ambiente Desktop di Xfce.</li>
	
	<li><a href="http://www.burtonini.com/blog/computers/devilspie">
	Devil's Pie</a> : un tool per la manipolazione delle finestre. Devil's Pie può essere 
	configurato, ad esempio,  per far in modo che le finesre siano visualizzabili in tutti gli 
	ambienti di lavoro o che non risultino visibili nella taskbar.</li>
	
	<li><a href="http://hocwp.free.fr/xbindkeys/xbindkeys.html">
	XBindKeys</a> : un programma che permette di lanciare comandi da shell con la  
	tastiera o il mouse sotto X Window.</li>
	
</ul>

<h3>Collegamenti vari :</h3>

<ul class="submenu">

	<li><a href="http://catb.org/~esr/faqs/smart-questions.html">
	La via più breve per fare domande</a></li>

	<li><a href="http://www.gtk.org/">
	GTK+</a> : GTK+ è un toolkit multi-piattaforma per creare interfacce 
	grafiche per gli utenti.</li>

	<li><a href="http://www.freedesktop.org">
	Freedesktop.org</a> : un progetto free software per lavorare sull'interoperabilità e  
	tecnologia di scambio per gli ambienti desktop per l'X Window System.</li>
	
	<li><a href="http://www.lunar-linux.org/">
	Lunar-linux</a> : una distribuzione linux basata su sorgente con un unico 
	pacchetto per gestire il sistema.</li>

	<li><a href="http://www.xfld.org/">Xfld</a> : Xfld è un 'Xfce live demo', 
	un sistema operativo GNU/Linux (derivato da Knoppix) che può girare 
	completamente da CD. La caratteristica è che come ambiente desktop viene utilizzato Xfce.</li>
	
	<li><a href="http://www.os-cillation.com/">os-cillation</a> : os-cillation e.K. 
	è una compagnia IT specializzata in sviluppo software, consulenze tecniche 
	e sviluppo di progetti e soluzioni server, localizzata a Siegen/Germania. 
	Fornisce supporto commerciale per Xfce, e 
	<a href="http://www.os-cillation.com/article.php?sid=37">Pacchetti Debian</a> 
	per Xfce 4.1cvs</li>
	
	<li><a href="http://cobind.com/index.html">
	Cobind</a> : "Basato sul cuore di Linux Fedora Core, Cobind Desktop sposa Xfce e 
	Nautilus all'interno di una coesiva esperienza desktop"</li>
		
	<li><a href="http://spblinux.sourceforge.net/">SPB-Linux</a> : una piccola, stabile, 
	mini distrubuzione linux che gira completamente su ramdisk, può essere avviata 
	da un unità di immagazinamento usb, ed ora esegue Xfce 4.2
	</li>
		
	<li><a href="http://bluefish.openoffice.nl/index.html">
	Bluefish</a> : un potente editor per web designer e 
	programmatori.</li>
	
	<li><a href="http://www.themedepot.org/">
	Themedepot.org</a> : fornisce una selezione di temi. Inclusa un area per Xfce.</li>
	
	<li><a href="http://www.freesbie.org/">
	FreeSBIE</a> : un sistema basato su FreeBSD, eseguibile direttamente da CD 
	(live CD) che ora include il nuovo Ambiente Desktop di Xfce 4.</li>
	<li><a href="http://www.zenwalk.org">ZenWalk</a>:  ZenWalk Linux distribution includes Xfce 4.2 as its default and unique Desktop Environment. It aims to be focused on Internet application, multimedia and coding tools.</li>
</ul>

