<h3>推荐的应用程序和工具：</h3>

<ul class="submenu">
<li><a href="http://rox.sourceforge.net/phpwiki/index.php/ROX-Filer">ROX文件管理器</a> : 一个轻量级的但是强大的文件管理器，可以在桌面上放置程序图标和把运行的程序缩小为桌面图标。</li>
<li><a href="http://www.os-cillation.com/article.php?sid=40">Terminal</a> : a modern terminal emulator for the Unix/Linux desktop - primarly for the Xfce desktop environment.</li>
<li><a href="http://www.burtonini.com/blog/computers/devilspie">Devil派</a> : 一个窗口操作工作。Devil派是高可配置的，例如让一个窗口显示在所有的工作空间上或不显示在任务条上。</li>
<li><a href="http://hocwp.free.fr/xbindkeys/xbindkeys.html">XBindKeys</a> : 允许通过键盘快捷键或鼠标键执行shell命令的X Windows程序。</li>
</ul>

