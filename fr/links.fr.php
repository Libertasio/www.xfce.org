<h3>Applications et utilitaires recommandés :</h3>

<ul class="submenu">
<li><a href="http://rox.sourceforge.net/phpwiki/index.php/ROX-Filer">ROX filer</a> : un gestionnaire de fichiers léger mais puissant, qui autorise la création de lanceurs sur le bureau et d'icônes pour les fenêtres réduites.</li>
<li><a href="http://www.os-cillation.com/article.php?sid=40">Terminal</a> : 	
un émulateur de terminal moderne pour les bureaux Unix/Linux - conçu d'abord pour l'environnement Xfce.</li>
<li><a href="http://www.burtonini.com/blog/computers/devilspie">Devil's Pie</a> : un outils de manipulation des fenêtres. Devil's Pie peut être configuré, par exemple, pour faire apparaître une fenêtre sur tous les bureaux, ou ne pas la faire apparaître dans la barre des tâches.</li>
<li><a href="http://hocwp.free.fr/xbindkeys/xbindkeys.html">XBindKeys</a> : un programme qui vous permet de lancer des commandes shell au moyen de votre clavier ou de votre souris sous X Window.</li>
</ul>

<h3>Liens variés :</h3>

<ul class="submenu">
<li><a href="http://catb.org/~esr/faqs/smart-questions.html">How To Ask Questions The Smart Way</a></li>
<li><a href="http://www.gtk.org/">GTK+</a> : GTK+ est une boîte à outil multi-plateformes destinée à la création d'interfaces graphique utilisateur.</li>
<li><a href="http://www.freedesktop.org">Freedesktop.org</a> : un projet de logiciel libre qui travaille sur l'interopérabilité et le partage de la technologie entre les environnements de bureau fonctionnant sous le système X Window.</li>
<li><a href="http://www.lunar-linux.org/">Lunar-linux</a> : une distribution Linux basée sur le code source avec un système unique de gestion des paquetages.</li>
<li><a href="http://www.xfld.org/">Xfld</a> : Xfld est une démonstration 'Xfce live', un système d'exploitation GNU/Linux (dérivé de Knoppix) qui peut fonctionner entièrement depuis un CD. Il utilise l'environnement de bureau Xfce dans sa toute dernière version de développement.</li>
<li><a href="http://www.os-cillation.com/">os-cillation</a> : os-cillation e.K. est une société spécialisée dans le développement logiciel, l'expertise technique, la gestion de projet, aussi bien que dans les solutions serveurs, localisée à Siegen en Allemagne. Elle propose un support comercial pour Xfce, et des <a href="http://www.os-cillation.com/article.php?sid=37">paquets Debian</a> de la version 4.1cvs de Xfce.</li>
<li><a href="http://cobind.com/index.html">	Cobind</a> : "Based on Fedora Core Linux, Cobind Desktop marries Xfce and Nautilus into a cohesive desktop experience"</li>
	
	<li><a href="http://spblinux.sourceforge.net/">SPB-Linux</a> : a small, stable, 
	up to date linux mini distribution which runs completely in ramdisk, can be booted 
	from a usb storage media, and now runs Xfce 4.2
	</li>
	
<li><a href="http://bluefish.openoffice.nl/index.html">Bluefish</a> : un puissant éditeur pour les concepteurs de sites internet et les programmeurs.</li>
<li><a href="http://www.themedepot.org/">Themedepot.org</a> : propose une sélection de thèmes.</li>
<li><a href="http://www.freesbie.org/">FreeSBIE</a> : un système basé sur FreeBSD, bootable directement depuis le CD, qui inclut maintenant le nouvel environnement de bureau Xfce 4.</li>
<li><a href="http://www.zenwalk.org">ZenWalk</a>:  la distribution ZenWalk Linux utilise Xfce 4.2 comme environnement de bureau exclusif. Elle a pour particularité d'être orientée vers les applications internet, multimedia et sur les outils de développement.</li>
</ul>

