<p>Il y a plusieurs paquets additionnels qui ne comptent pas parmi les éléments principaux du bureau XFce 4, et qui fournissent des thèmes ou des fonctionnalités supplémentaires. Certains d'entre-eux font partie de la dernière version de XFce 4, d'autres sont encore en cours de développement.</p>

