<h1>設定マネージャ</h1>
<p>この設定マネージャはMCS (Multi-Channel Settings, つまり、多チャンネル設定)、モジュラ
、集約化された設定システム(ホストに依存しない上に、ネットワークとの親和性があります。)を提供しています。
バックグランドで動き、全体の設定情報を他のXfceコンポーネントに渡します。
すべてのコンポーネントの設定ダイアログにアクセスするためのメインコントロールパネルがあります。
MCS プラグインは総合的なもの(つまり、パッケージの一部ではないもの。たとえば、GTK、マウス、フォント、キーボード、ディスプレイの設定)
用の設定ダイアログを提供します。</p>

