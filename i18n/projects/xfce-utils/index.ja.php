<h1>ユーティリティとスクリプト</h1>

<p>
ユーティリティとスクリプトは、Xfce 4 のアバウトダイアログ、アプリケーションランチャ、およびパネルやデスクトップメニューといった他の Xfce コンポーネントでも使用されるいくつかの役に立つスクリプトを提供します。
</p>

<h2>機能</h2>

<ul>
  <li>アバウトダイアログ</li>
  <li>アプリケーションランチャ</li>
  <li>スクリプト: X セッションの初期化、画面のロック、およびローカルドキュメントの表示</li>
</ul>

