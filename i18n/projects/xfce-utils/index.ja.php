<h1>ユーティリティとスクリプト</h1>
<p>ユーティリティとスクリプトは「Xfceについて」ダイアログ、アプリケーションランチャー、
パネルやデスクトップメニューなどの他のXfceコンポーネントでも使えるいくつかの便利なスクリプト
を提供しています。</p>

<h2>機能</h2>
<ul>
  <li>Xfceについて ダイアログ</li>
  <li>アプリケーションランチャー</li>
  <li>スクリプト：Xのセッションを起動するためのもの、スクリーンをロックするためのもの、ローカルにある文書を開くためのもの。</li>
</ul>

