<h1>实用程序与脚本</h1>
<p>实用程序与脚本提供了一个Xfce 4的关于对话框，一个程序起动器以及许多同样被其他Xfce组件（例如面板和桌面菜单）所使用的脚本。</p>

<h2>特性</h2>
<ul>
  <li>关于对话框。</li>
  <li>程序起动器。</li>
  <li>脚本：初始化X会话、锁住屏幕以及打开本地文档。</li>
</ul>

