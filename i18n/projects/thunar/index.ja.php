<h1>Thunarファイルマネージャ</h1>
<p>Thunarは新しくて目新しいXfce デスクトップ環境用のファイルマネージャです。
Thunarは速く、そして簡単に使えるようにするために一から作りられました。
ユーザインターフェイスはすっきりしていて、直感的です。ユーザが混乱するようなオプションや
意味のないオプションはいっさい存在しません。Thunarは速く、起動時やディレクトリの読み取り時の反応がとても速いです。</p>
<p>ThunarのFreeDesktop.orgの標準に従い実装されています。
これは今日のUNIX/Linuxのデスクトップ世界にうまく溶け込めるようにするためです。つまり、システムがFreeDesktop.orgの標準にしたがっている
限りにおいては、特別な方法でセットアップする必要はありませんし、
そうでなければ、ファイルマネージャの設定に数時間掛ける必要もありません。</p>

