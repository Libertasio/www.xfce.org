<h1>Thunar文件管理器</h1>
<p>Thunar是用于Xfce桌面环境的新的现代的文件管理器。Thunar被重新设计成快速而易用。它的界面简洁直观，没有任何迷惑无用的东西。Thunar的启动和目录载入速度都很快。</p>
<p>Thunar贯彻FreeDesktop.org标准以使其很好得整合进现今的UNIX/Linux桌面世界。这意味着你无需为你的系统作特殊设置或花好几个小时来配置文件管理器，相反，如果你的系统编译了FreeDesktop.org标准，它会工作得很好。</p>

