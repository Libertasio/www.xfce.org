<h2>库</h2>
<p>核心库为 Xfce 环境提供基本函数。这些函数用来实现非图形帮助、窗口部件、图形用户界面的一些方便的功能以及设置管理功能。</p>
<h2>特性</h2>
<ul>
  <li><strong>libxfce4util</strong>: 基本工具。</li>
  <li><strong>libxfcegui4</strong>: 统一的窗口部件。</li>
  <li><strong>libxfce4mcs</strong>: Xfce 组件。</li>
</ul>

