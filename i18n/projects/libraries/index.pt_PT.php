<h1>Bibliotecas</h1>
<p>As bibliotecas base oferecem funcionalidades comuns ao ambiente Xfce. Existem três bibliotecas que oferecem
  funções não gráficas, engenhocas e funções convenientes para interface gráfica de utilizador, e funções para 
  gestão de definições.</p>

<h1>Particularidades</h1>
<ul>
  <li><strong>libxfce4util</strong>: utilidades básicas.</li>
  <li><strong>libxfcegui4</strong>: engenhocas unificadas.</li>
  <li><strong>libxfce4mcs</strong>: componentes Xfce.</li>
</ul>

