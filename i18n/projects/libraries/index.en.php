<h1>Libraries</h1>
<p>The core librairies provide common functionnalities to the Xfce environment. There are three libraries which provides
  non-graphical helper functions, widgets and convenience functions for graphical user interfaces, and functions for 
  settings management.</p>

<h1>Features</h1>
<ul>
  <li><strong>libxfce4util</strong>: basic utilities.</li>
  <li><strong>libxfcegui4</strong>: unified widgets.</li>
  <li><strong>libxfce4mcs</strong>: Xfce components.</li>
</ul>

