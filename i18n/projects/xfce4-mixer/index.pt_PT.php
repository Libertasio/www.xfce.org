<h1>Volume Control</h1>
<p>Este é o xfce4-mixer, uma aplicação de controlo volume para o Ambiente de Desktop Xfce. Este pacote de software contem 
   um plugin para controlo de volume para o Xfce Panel e uma aplicação mixer standalone.</p>

<p>Actualmente a aplicação de volume de controlo suporta os seguintes backends:</p>

<ul>
  <li><strong>OSS - Open Sound System</strong><br />
    Suportado em Linux, FreeBSD (newpcm), NetBSD/OpenBSD (libossaudio) e diversos outros derivados Unix.</li>

  <li><strong>SUN - Sun Audio</strong><br />
    Suportado no Sun Solaris.</li>
 
  <li><strong>ALSA - Advanced Linux Sound Architecture</strong><br />
    Suportado em novos sistemas Linux (2.4 e superior).</li>
</ul>

