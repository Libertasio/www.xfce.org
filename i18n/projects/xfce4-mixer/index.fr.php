<h1>contrôle du volume</h1>
<p>Le contrôle du volume, dans l'environnement de bureau Xfce, se fait via xfce4-mixer, une application servant à régler les volumes des différentes sorties de la carte son. Ce paquet contient un greffon de coontrôle du volume pour le panneau Xfce mais aussi une application de réglages des différents volumes.</p>

<p>L'application de contrôle des volumes supporte les ressources suivantes :</p>

<ul>
  <li><strong>OSS - Open Sound System</strong><br />
    Supporté sous Linux, FreeBSD (newpcm), NetBSD/OpenBSD (libossaudio) et autres dérivés de UNIX.</li>

  <li><strong>SUN - Sun Audio</strong><br />
    Supporté sur Sun Solaris.</li>
 
  <li><strong>ALSA - Advanced Linux Sound Architecture</strong><br />
    Supporté sur les systèmes Linux récents (2.4 et supérieurs).</li>
</ul>

