<h1>音量控制器</h1>
<p>xfce4-mixer，Xfce桌面环境的一个音量控制程序。该软件包包含一个音量控制面板插件和一个独立的混音程序。</p>

<p>目前此程序支持下列后端：</p>

<ul>
  <li><strong>OSS - Open Sound System</strong><br />
    适用于Linux、FreeBSD (newpcm)、NetBSD/OpenBSD (libossaudio)以及其他Unix派生。</li>

  <li><strong>SUN - Sun Audio</strong><br />
    适用于Sun Solaris。</li>
 
  <li><strong>ALSA - Advanced Linux Sound Architecture</strong><br />
    适用于Linux内核2.4及以上。</li>
</ul>

