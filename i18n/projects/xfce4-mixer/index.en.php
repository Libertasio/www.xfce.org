<h1>Volume Control</h1>
<p>This is xfce4-mixer, a volume control application for the Xfce Desktop Environment. This software package contains 
  both a volume control plugin for the Xfce Panel and a standalone mixer application.</p>

<p>Currently the volume control application supports the following backends:</p>

<ul>
  <li><strong>OSS - Open Sound System</strong><br />
    Supported on Linux, FreeBSD (newpcm), NetBSD/OpenBSD (libossaudio) and several other Unix-derivates.</li>

  <li><strong>SUN - Sun Audio</strong><br />
    Supported on Sun Solaris.</li>
 
  <li><strong>ALSA - Advanced Linux Sound Architecture</strong><br />
    Supported on newer Linux systems (2.4 and above).</li>
</ul>

