<h1>Xfmedia</h1>
<p>Xfmedia is a simple, easy-to-use media player based on the xine engine. The GTK+ GUI focuses on playing and managing 
  audio files, but, being based on xine, supports video as well. It has optional support for remote control with DBUS, 
  and for metadata read/write access with TagLib.</p>

