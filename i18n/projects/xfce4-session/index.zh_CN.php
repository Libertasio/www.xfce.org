<h1>会话管理器</h1>
<p>会话管理器允许用户保存会话以便下次回复到上次登出的状态。它能够保存许多不同的会话。它包括 3 个飞溅屏幕引擎。至少，它能够让您登出、重启以及关机。</p>

<h2>特性</h2>
<ul>
  <li>多显示支持。</li>
  <li>多个会话。</li>
  <li>管理远程程序。</li>
  <li>飞溅屏幕。</li>
  <li>登出、重启、关机。</li>
</ul>

