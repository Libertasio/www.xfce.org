<h1>セッションマネージャ</h1>
<p>セッションマネージャはユーザがセッションを記録したり、それをログイン時に
元に戻すようにできるために作られました。
三つのスプラッシュエンジンが一緒についています。さらに、これはログアウト、再起動、システムのシャットダウンをしやすくします。</p>

<h2>機能</h2>
<ul>
  <li>多画面ディスプレイ</li>
  <li>多セッション</li>
  <li>遠隔アプリケーションの管理</li>
  <li>スプラッシュ画面</li>
  <li>ログアウト、再起動、シャットダウン</li>
</ul>

