<h1>セッションマネージャ</h1>

<p>
セッションマネージャは、セッションの保存およびログイン時の復元を可能にします。
いくつかの異なるセッションの保存も行えます。また、ログアウトや、コンピュータの再起動、シャットダウン、ハイバネート、およびサスペンドを簡単に行うことができます。
</p>

