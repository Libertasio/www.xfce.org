<h1>Session Manager</h1>
<p>The session manager allows the user to save sessions and restore them after
  login. It is capable of saving several different sessions. It comes with
  three splash screen engines. And at last it helps you to log out, reboot, and
  shutdown the system.</p>

<h2>Features</h2>
<ul>
  <li>Multiscreen display.</li>
  <li>Multiple sessions.</li>
  <li>Manage remote applications.</li>
  <li>Splash screen.</li>
  <li>Log out, reboot, and shutdown.</li>
</ul>

