<h1>Orage</h1>
<p>Orage provides a calendar which integrates nicely into the Xfce desktop environment. It is highly configurable and 
  supports alerts based on dates. It warns you with popup or audio alarm. As it is an application kind-of every day use
  it launches itself in the background as a daemon.</p>

