<h1>Orage</h1>
<p>OrageはXfceデスクトップ環境向けにとても良く調整されたカレンダーです。
とても高度に設定でき、アラートもサポートしています。アラートはオーディオアラームとポップアップどちらかで行われます。
これは日常的に使われるアプリケーションなので、デーモンとしてバックグランドで起動されます。</p>

