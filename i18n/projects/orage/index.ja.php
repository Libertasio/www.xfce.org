<h2>Orage</h2>

<p>
Orage は、Xfce デスクトップ環境とよく統合されたカレンダを提供します。
高度に設定可能であり、日付ベースのアラート機能を備え、ポップアップまたはオーディオアラームによってあなたにお知らせします。
毎日使うアプリケーションなので、デーモンとしてバックグラウンドで起動でき、パネル用の Orage クロックプラグインを使用してアクセスできます。
</p>

