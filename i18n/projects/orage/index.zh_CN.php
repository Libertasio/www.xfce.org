<h1>Orage</h1>
<p>Orage 提供一个与 Xfce 桌面环境很好得整合的日历程序。它极具定制性并且拥有日期提醒功能。它通过弹出信息或播放音频文件来进行提醒。通常您每天都会用到这个程序，所以它以守护进程的形式在后台运行。</p>

