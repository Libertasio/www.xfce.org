<h1>Orage</h1>
<p>Orage oferece um calendário que se integra bem no ambiente Xfce. É altamente configurável e
  suporta alertas baseados em datas. Dá aviso com popup ou alarme áudio. Como é uma aplicação para uso diário 
  é lançada como daemon em segundo plano.</p>

