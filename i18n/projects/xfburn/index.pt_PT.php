<h1>Xfburn</h1>
<p>Xfburn é uma simples ferramente de queima de CD que age como front-end para o mkisofs, cdrdao, readcd e cdrecord. Pode apagar
  CD-RW, copiar CDs, queimar e criar imagens iso, e queimar composição pessoal de dados.</p>

