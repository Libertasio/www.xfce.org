<h1>Xfburn</h1>
<p>Xfburn is a simple CD burning tool acting as a front-end to mkisofs, cdrdao, readcd and cdrecord. It can blank
  CD-RW, copy CDs, burn and create iso images, and burn personal composition of data.</p>

