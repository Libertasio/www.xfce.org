<h1>Xfburn</h1>
<p>Xfburnは簡素なCD作成ツールです。これはmkisofs, cdrdao, readcd, cdrecordのフロントエンドとして動作します。
CD-RWを初期化したり、CDをコピーしたり、新しいisoイメージを作成しCDに焼き付けたり、個人用データを含んだCDを作成したり
することができます。</p>

