<h1>Xfburn</h1>
<p>Xfburn是一个简单的CD刻录工具，作为mkisofs、cdrdao、readcd和cdrecord的前端。它能清空CD-RW、复制CD、刻录和创建iso映像以及刻录您个人组合的数据。</p>

