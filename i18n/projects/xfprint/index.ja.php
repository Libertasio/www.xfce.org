<h1>印刷ヘルパ</h1>

<p>
印刷ヘルパは、印刷、プリンタ管理、およびジョブキュー管理のフロントエンドです。
プリンタ自身の設定はできませんが、適切に設定されている印刷システムのみを使用します。
</p>

