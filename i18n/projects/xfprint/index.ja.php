<h1>プリントマネージャ</h1>
<p>プリントマネージャはグラフィカルな印刷フロントエンドです。これは
プリンタの管理、ジョブキューの管理を提供します。これ自身は
プリンタの設定をユーザに要求しません。なぜなら、すでに適切に設定されている
印刷システム(CUPSやBSD-LPRから得たプリンタ情報)を利用するからです。</p>

<h2>サポートされているバックエンド</h2>
<ul>
  <li><strong>CUPS</strong></li>
</ul>
<ul>
  <li><strong>BSD-LPR</strong></li>
</ul>
<ul>
  <li><strong>ファイル (PostScript)</strong></li>
</ul>

