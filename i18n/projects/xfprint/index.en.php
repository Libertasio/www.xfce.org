<h1>Printing Helper</h1>
<p>The printing helper is a graphical frontend for printing, a printer 
  management, and a job queue management. It doesn't let you configure printers 
  but only use printing systems that have already been configured properly.</p>

<h2>Supported backends</h2>
<ul>
  <li><strong>CUPS</strong></li>
</ul>
<ul>
  <li><strong>BSD-LPR</strong></li>
</ul>
<ul>
  <li><strong>File (PostScript)</strong></li>
</ul>

