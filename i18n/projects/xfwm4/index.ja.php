<h1>ウィンドウマネージャ</h1>
<p>Xfce 4 ウィンドウマネージャは画面上のアプリケーションウィンドウの配置を管理し、
美しいウィンドウの装飾を提供します。さらに、ワークスペースや仮想デスクトップ
を管理し、多画面もこれ自身に実装されています。透過や影をサポートする(Xorg コンポジット拡張からの)コンポジションマネージャ
も含まれています。Xfce 4 ウィンドウマネージャはユーザが任意で特定のコマンドや基本的な操作ができるようにキーボードショートカット設定できる
キーボードショートカットエディタを上級者向けに高度な設定をするためのダイアログも付属します。
</p>

<h2>機能</h2>
<ul>
  <li>ソフト自身に実装されたXineramaやTwinview用の多画面サポート</li>
  <li>ウィンドウの装飾に使える多数の画像フォーマットのサポート(xpm, png, svg, bmp, jpg, 
    gif).</li>
  <li>ウィンドウとワークスペーションの操作とユーザが任意のコマンドを設定するためのショートカットエディタ </li>
  <li>高度な焦点の実現性</li>
  <li>ウィンドウとデスクトップをある一定の範囲内でしか移動できないようにする機能とウィンドウがバーにくっつく機能</li>
  <li>ソフト内部に実装された透過ウィンドウと影を表示できるようにするコンポジッター</li>
  <li>ワークスペースをマウスホイールを回すことにより切り替えられる機能</li>
</ul>

