<h2>ウィンドウマネージャ</h2>

<p class="center"><img src="/images/projects/screenshots/xfwm4.png" alt="Window Manager" /></p>

<p>
Xfce ウィンドウマネージャは、画面上のアプリケーションの配置、ウィンドウの装飾、ワークスペースまたは仮想デスクトップを管理します。また、マルチスクリーンをネイティブでサポートしています。
