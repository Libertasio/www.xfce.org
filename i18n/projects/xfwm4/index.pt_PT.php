<h1>Gestor de Janelas</h1>
<p>O gestor de janelas do Xfce 4 gere a posição das janelas
  das aplicações no ecrã, oferece belas decorações de janelas, gere
  áreas de trabalho ou desktops virtuais, e suporta o nativamente modo
  multiscreen. Oferece o seu próprio gestor de composição (da extensão de
  composição Xorg) para transparências e sombras reais. O gestor de janelas do Xfce 4
  inclui um editor de atalhos do teclado para comandos específicos do utilizador e
  manipulações básicas de janelas, e oferece um diálogo para ajustes avançados.</p>

<h2>Características</h2>
<ul>
  <li>Suporte Multihead nativo para Xinerama e Twinview.</li>
  <li>Suporte a muitos formatos de imagem para decoração de janelas (xpm, png, svg, bmp, jpg,
    gif).</li>
  <li>Editor de atalhos para manipulação de janelas e áreas de trabalho e para
    comandos específicos do utilizador.</li>
  <li>Possibilidades de foco avançadas.</li>
  <li>Resistência ou magnetização de janelas e da área de trabalho.</li>
  <li>Compositor próprio para janelas transparentes e sombras.</li>
  <li>Roda do rato sobre o desktop troca a área de trabalho.</li>
</ul>

