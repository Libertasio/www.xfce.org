<h1>设置管理器</h1>
<p>设置管理器提供一个 MCS (Multi-Channel Settings|多频道设置)、模块化的、不依赖于宿主机的、网络透明的集中设置系统。它在后台运行，为其他Xfce组件提供全局设置信息。它通过一个主设置面板来访问所有的组件设置对话框。其中的MCS插件为其他不是软件包组成部分的常用项目提供了设置对话框，例如GTK、鼠标、字体、键盘和显示设置。</p>

<h2>特性</h2>
<ul>
  <li><strong>硬件组件</strong>:
    显示、
    鼠标、
    键盘以及
    声音。
  </li>
</ul>
<ul>
  <li><strong>Xfce组件</strong>:
      主题和装饰、
      桌面、 
      面板、
      文件管理器、
      日历、
      飞溅屏幕、
      会话和启动以及
      首选应用程序。
  </li>
</ul>

