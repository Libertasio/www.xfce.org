<h1>Settings Manager</h1>

<h2>Description</h2>
<p>The settings manager. It provides the Multi-Channel Settings, a modular, host
  independent, network transparent and centralized configuration system. It runs
  in the background and provides global settings information to other Xfce 
  components. There is a main control panel to access the configuration dialogs 
  of Xfce components. Its icons are customizable. The MCS plugins provide 
  settings dialogs for general items that are not part of a package, e.g. GTK, 
  mouse, fonts, keyboard and display settings.</p>

<h2>Features</h2>
<ul>
  <li>Basic hardware components</li>
  <ul>
    <li>Display: resolution, gamma</li>
    <li>Mouse: bahavior, cursor theme</li>
    <li>Keyboard: typing settings, shortcut editor, accessibility</li>
    <li>Sound: device and controls selector</li>
  </ul>
</ul>
<ul>
  <li>Xfce components</li>
  <ul>
    <li>Theme and Decoration</li>
    <li>Desktop, background</li>
    <li>File Manager</li>
    <li>Calendar</li>
    <li>Preferred Applications</li>
    <li>Panel</li>
    <li>Sessions and Startup</li>
    <li>Splash</li>
  </ul>
</ul>

