<h1>Gestor de Definições</h1>
<p>O Gestor de Definições oferece o MCS (Multi-Channel Settings), um modular, 
  host independente, transparente e centralizado sistema de configuração de rede. 
  Corre em segundo plano e oferece definições globais de informação a outros 
  componentes Xfce. Existe um painel de controlo principal com diálogos para aceder à configuração 
  de todos os componentes. Os plugins MCS oferecem diálogos de definições para itens gerais
  que não são parte de um pacote, e.x. GTK, rato, fontes, 
  teclado e definições de ecrã.</p>

