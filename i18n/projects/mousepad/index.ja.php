<h1>マウスパッド</h1>
<p>マウスパッドはLeafpadを元にした簡素なテキストエディタです。
マウスパッドを開発することにした理由は印刷サポートを提供することでした。
これはいくつかの理由でLeafpadに実装するのは難しいものでした。</p>

