<h1>マウスパッド</h1>

<p class="center"><img src="/images/projects/screenshots/mousepad.png" alt="Mousepad" /></p>

<p>
マウスパッドは、Leafpad をベースとしたシンプルなテキストエディタです。
様々な理由で Leafpad では実装が困難だった印刷機能を提供しています。
</p>

