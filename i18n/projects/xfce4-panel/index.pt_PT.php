<h1>Painel</h1>
<p>O Painel Xfce 4 suporta múltiplos painéis, com muitas opções para a
  posição, aparência, transparência e comportamento. Existem muitos itens 
  disponíveis por padrão para preencher um painel, como lançadores de aplicação com 
  menus destacáveis, um pager gráfico, uma lista de tarefas, um relógio, uma bandeja de sistema, 
  um alternador de desktop mostra/oculta e ainda mais. Oferece uma maneira fácil de adicionar 
  itens usando um diálogo e mover itens para diferentes panéis. É 
  compatível com os numerosos plugins de painel disponíveis em 
  <a href="http://goodies.xfce.org/">Xfce Goodies Project page</a>.</p>

<h2>Características</h2>
<ul>
  <li><strong>Múltiplos panéis</strong> com suporte múltiplo a ecrã.</li>
</ul>
<ul>
  <li><strong>Aparência Personalizada</strong>:
    posição,
    tamanho,
    ocultar automaticamente e
    transparência com Xorg Composite extension.</li>
</ul>
<ul>
  <li><strong>Plugins</strong>:
    menu, 
    lançador, 
    lista de tarefas, 
    relógio, 
    bandeja de sistema, 
    pager, 
    mostrar desktop e
    sair ou bloquear ecrã.</li>
</ul>
<ul>
  <li><strong>Plugins de painel avançados</strong>: 
    monitorização de sistema e rede,
    gestor de clipboard,
    notas,
    entrada de linha de comando,
    data e hora,
    e outros plugins úteis para monitorizar 
    a bateria do portátil,
    estado de Wi-Fi,
    ou monitorizar a frequência do CPU...</li>
</ul>

