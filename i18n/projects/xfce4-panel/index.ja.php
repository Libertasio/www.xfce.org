<h1>パネル</h1>
<p>Xfce 4 パネルはたくさんのオプション（場所、装飾、透過、動作を設定するもの）を実装した複数のパネルです。
デフォルトでもたくさんのものが利用できます。たとえば、パネルを完全に取り外しできるメニューがついたアプリケーションランチャー、。
グラフィカルページャ、タスクリスト、時計、システムトレイ、デスクトップで表示中のアプリを最小化・最大化するスイッチャーなどです。
簡単にダイアログを使ってアイテムを追加でき、他のパネルの中にもアイテムを動かすことができます。
<a href="http://goodies.xfce.org/">Xfce Goodies Project</a>にある数多くのプラグインと互換性があります。</p>

<h2>機能</h2>
<ul>
  <li><strong>多数のパネル</strong> （同時に多画面のサポート）</li>
</ul>
<ul>
  <li><strong>カスタマイズできる外観</strong>:
    位置
    大きさ
    自動的に隠すかどうかの設定
    透過性（Xorg Composite extensionを利用しています）</li>
</ul>
<ul>
  <li><strong>プラグイン</strong>:
    メニュー 
    ランチャー 
    タスクリスト 
    時計
    システムトレイ 
    ページャ 
    デスクトップの表示
    ログアウト・ロックのスイッチ</li>
</ul>
<ul>
  <li><strong>高度なパネルプラグイン</strong>: 
    システムとネットワークのモニタ
    クリップボードマネージャ
    ノート
    コマンドラインエントリ
    暦と時間
    そして、ラップトップ用に有用なプラグインは：
    バッテリーモニター
    Wi-Fi ステータス
    CPUの動作周波数モニター</li>
</ul>

