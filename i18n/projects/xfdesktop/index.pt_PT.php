<h1>Gestor de Desktop</h1>
<p>O gestor de desktop muda a imagem de fundo, oferece um menu de botão direito para lançar aplicações e opcionalmente 
  mostra ficheiros (incluindo lançadores de aplicação) ou iconifica janelas. Inclui suporte de gradiente à cor de fundo, 
  suporte a saturação de imagem de fundo, suporte real a multiscreen e xinerama e oferece um editor de menu de desktop.</p>

<h2>Características</h2>
<ul>
  <li><strong>Fundo de Ecrã</strong><br />
    Cor plana e gradiente.<br />
    Imagem com luminosidade ajustável.</li>
</ul>
<ul>
  <li><strong>Ícones</strong><br />
    Janelas iconificadas.<br />
    Ficheiros e entradas de desktop (lançadores de aplicações).</li>
</ul>
<ul>
  <li><strong>Menu</strong><br />
    Clique direito para menu padrão do Xfce.<br />
    Clique do meio para lista de aplicações.<br />
    Personalizável com um editor.</li>
</ul>

