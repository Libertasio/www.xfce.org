<h1>Desktop Manager</h1>
<p>The desktop manager sets the background image, provides a right-click menu to launch applications and can optionally 
  show files (including application launchers) or iconified windows. It includes gradient support for background color, 
  saturation support for background image, real multiscreen and xinerama support, and it provides a desktop menu 
  editor.</p>

<h2>Features</h2>
<ul>
  <li><strong>Background</strong><br />
    Plain color and gradient.<br />
    Image with adjustable brightness.</li>
</ul>
<ul>
  <li><strong>Icons</strong><br />
    Iconified windows.<br />
    Files and desktop entries (application launchers).</li>
</ul>
<ul>
  <li><strong>Menu</strong><br />
    Right click for default Xfce menu.<br />
    Middle click for application list.<br />
    Customizable with an editor.</li>
</ul>

