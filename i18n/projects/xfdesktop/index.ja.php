<h1>デスクトップマネージャ</h1>
<p>デスクトップマネージャは壁紙の設定や、アプリケーションを起動できる右クリックメニュー、
そして、任意ですがファイル(アプリケーションのランチャーを含む)を表示したり、ウィンドウを最小化する機能を提供します。
また、背景色の階調度や、壁紙の彩度の設定、多画面とxineramaのサポートを含んでいます。そして、デスクトップメニューエディタも
付属しています。</p>

<h2>機能</h2>
<ul>
  <li><strong>壁紙</strong><br />
    色設定と階調設定<br />
    明るさを設定できる画像</li>
</ul>
<ul>
  <li><strong>アイコン</strong><br />
    ウィンドウの最小化とアイコン化<br />
    ファイルとデスクトップエントリー(アプリケーションランチャー)</li>
</ul>
<ul>
  <li><strong>メニュー</strong><br />
    デフォルトのXfceメニューを表示する右クリック<br />
    アプリケーションリストを表示する中ボタン<br />
    エディタでカスタマイズできるメニュー</li>
</ul>

