<h1>桌面管理器</h1>
<p>桌面管理器用于设置桌面背景、提供一个右键菜单或者显示启动器或者最小化窗口图标。
  它支持背景颜色渐变，背景图片饱和度设置，多显示器以及 xinerama 支持，还有一个桌面菜单编辑器。</p>

<h2>特性</h2>
<ul>
  <li><strong>背景</strong><br />
    纯色和渐变。<br />
    图片可变亮度。</li>
</ul>
<ul>
  <li><strong>图标</strong><br />
    最小化窗口。<br />
    文件与桌面项(程序启动器)。</li>
</ul>
<ul>
  <li><strong>菜单</strong><br />
    右键菜单。<br />
    中键程序列表。<br />
    可编辑菜单。</li>
</ul>

