<h1>ターミナル</h1>
<p>ターミナルは目新しく、軽く、メモリをあまり食わないタブと多数のウィンドウをサポートしたXfceデスクトップ環境用の
ターミナルエミュレータです。キーボードショートカット、画面、色などをすべて好きなように設定できます。</p>

