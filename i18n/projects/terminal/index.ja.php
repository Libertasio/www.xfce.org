<h1>ターミナル</h1>

<p class="center"><img src="/images/projects/screenshots/terminal.png" alt="Terminal" /></p>

<p>
ターミナルは、Xfce デスクトップ環境向けの、最新で、軽量かつメモリ消費の少ないターミナルエミュレータで、タブおよび複数ウィンドウをサポートしています。
キーバインド、外観、配色などをフルカスタマイズできます。
</p>

