<h1>アーカイブマネージャ</h1>
<p>アーカイブマネージャは軽量でGTK+2を使ったいくつかのツール(7zip, bzip2, rar, zipなど)用のフロントエンドです。
簡単にアーカイブを解凍したり、新しいアーカイブを作成することをすばやくできるようにすることを目的としています。</p>

