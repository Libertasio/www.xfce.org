<h1>Archive Manager</h1>
<p>The Archive Manager is a lightweight GTK+2 only frontend to several tools like 7zip, bzip2, gzip, rar, zip, and 
  more. It helps you to quickly decompress an archive, or to prepare and create new archives.</p>

