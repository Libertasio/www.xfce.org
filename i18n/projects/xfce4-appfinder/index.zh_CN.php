<h1>应用程序查找器</h1>
<p>这是 Xfce 桌面环境的一个程序查找器。这是一个很有用的程序，它允许您查找您系统中所有支持桌面项格式的程序，然后将其加入菜单项中，然后当然是从菜单启动它了。</p>

