<h1>Application Finder</h1>
<p>Esta é uma aplicação de pesquisa para o ambiente Xfce. É um útil software que permite encontrar qualquer
  aplicação no sistema que suporte o formato de entrada Desktop. Pode procurar por software "perdido" e adicionar
  entradas ao menu e obviamente executar o software seleccionado.</p>

