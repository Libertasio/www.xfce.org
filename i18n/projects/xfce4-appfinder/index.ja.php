<h1>アプリファインダー</h1>
<p>これはXfce環境用のアプリファインダーです。
デスクトップエントリフォーマットに対応したシステム中のアプリケーションすべてを見つけ出してくれる便利なソフトです。
「見つけられない」ソフトウェアをこれを使って見つけることができ、メニューにエントリーを追加したり
このソフトから、もちろんのこと、選択したソフトを起動することができます。</p>

