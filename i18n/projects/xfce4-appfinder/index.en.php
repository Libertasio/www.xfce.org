<h1>Application Finder</h1>
<p>This is an application finder for the Xfce environment. It is an useful software that permits you to find every
  application in the system supporting Desktop entry format. You can search for "lost" software with it, and add
  entries to the menu, and obviously run the selected software from it.</p>

