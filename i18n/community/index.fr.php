<h1>Communauté Xfce</h1>
<p>Si vous n'avez pas réussi à résoudre votre problème à l'aide de la documentation de ce site Web, les communautés Xfce sont de bonnes places pour trouver de l'aide ou faire part de vos suggestions.<br />
Souscrivez à l'une des <a href="/community/lists">listes de distribution</a>,
faites une recherche sur le <a href="http://wiki.xfce.org/" target="_blank">wiki</a> de Xfce,
visitez le <a href="http://forum.xfce.org/" target="_blank">forum</a> ou joignez le <a href="/community/irc">canal IRC</a> de Xfce pour discuter avec d'autres utilisateurs de Xfce.</p>
