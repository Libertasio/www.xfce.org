<h1>Xfce IRC</h1>

<p>If you have a problem that you cannot resolve, you might want to try asking a 
question in our IRC channel <a href="irc://irc.freenode.net/#xfce">#xfce on irc.freenode.net</a>. The channel has several 
active users. It is recommended that you consult the <a href="/documentation">documentation</a> about your problem 
before asking your question.</p>