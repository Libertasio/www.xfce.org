<h1>Caractéristiques de Xfce</h1>
<ul>
  <li>Multi-platformes: Linux, NetBSD, FreeBSD, OpenBSD, Solaris, Cygwin, et MacOS X</li>
  <li>Environnement de bureau léger</li>
  <li>Logiciels simples et légers</li>
  <li>Goodies pour plus de fonctionnalités</li>
  <li>Support multi-écrans et « Xinerama »</li>
  <li>Mode « Kiosk » pour restreindre la configuration</li>
  <li>Traduit dans plus de 40 langues</li>
</ul>
<ul>
  <li>Conformité aux spécifications <a href="http://www.freedesktop.org/" target="_blank" class="external">Freedesktop</a>
  <ul>
    <li>Indices de gestionnaire de fenêtres étendus (EWMH)</li>
    <li>Menus du bureau</li>
    <li>Thèmes d'icônes</li>
    <li>Répertoires de base « XDG »</li>
    <li>Protocole « XSETTINGS »</li>
    <li>Protocole Glisser-Déposer (DND)</li>
  </ul></li>
</ul>

<p>Xfce fournit un environnement de bureau léger avec des icônes et thèmes par défaut.
  Tout d'abord, Xfce contient un gestionnaire de bureau avec icônes, de multiples 
  panneaux avec un menu par défaut, des logiciels et greffons ainsi qu'un
  gestionnaire de fenêtres supportant le « composite » Xorg pour la transparence et
  l'ombrage des fenêtres. De plus, Xfce contient un gestionnaire de sessions permettant
  d'éteindre et de redémarrer l'ordinateur. Toutes ces fonctions peuvent être
  réglées à partir du gestionnaire de paramètres de Xfce.</p>
<p>Xfce fournit des logiciels de base conçus pour être légers et intégrés à
  l'environnement de bureau. Ces applications sont : un émulateur de terminal avec
  onglets, un calendrier au format « iCal » pour les événements et rendez-vous,
  un gestionnaire de son pour contrôler le niveau des volumes. Pour certaines de ces
  applications existe un greffon pour le panneau. Par exemple, le calendrier a 
  un greffon configurable qui affiche la date et l'heure, le gestionnaire de son
  permet d'ajouter un greffon contrôlant le canal maître.</p>
<p>Pour conclure, Xfce offre des logiciels toujours conçus pour la légèreté :
  un éditeur de texte simple et rapide avec fonctions d'impression, un gestionnaire
  de fichiers simple d'utilisation, un lecteur multimédia basé sur Xine, un logiciel de gravure de CDs et DVDs, et un gestionnaire d'archives pour de nombreux formats.</p>

