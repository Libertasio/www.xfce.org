<h2>Leiho Kudeatzaile Ezaugarriak</h2>
<p>Xfwm4, Xfce leiho kudeatzaileak, berrikuntza ugari jasan ditu.
Erabiltzaileak ikusten duen aldaketa horietako interesgarriena leiho
kudeatzaile ezaugarri elkarrizketa da, honek erabiltzaileari leiho
kudeatzailearen zenbait hedaturiko aukera ezartzeko aukera ematen
dio.</p>
