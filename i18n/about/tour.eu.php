<h1>fce 4.4 Ikustaldia</h1>
<p>Xfce 4.4-ek azken argitaratze nagusitik ezaugarri berri mordoa
ditu. Orrialde honek Xfce 4.4-eko aldaketa nagusien zerrenda bat
bistarazi beharko luke. Bertan ez da <a href="http://thunar.xfce.org" target="_blank">Thunar</a> sartzen.</p>

<h2>Idazmahai Ikonoak</h2>
<p>Xfce 4.0/4.2 egunetan gehien eskatu zen ezaugarrietako bat
idazmahai ikonoak izan ziren. Xfce 4.4-ek azkenik idazmahaiean
ikonoak onartzen ditu.</p>
<div class="image">
	<a href="/images/about/tour/desktop-icons.png">
		<img src="/images/about/tour/desktop-icons-thumb.png" border="0" alt="desktop icons" />
	</a>
</div>
</p>
<p>Xfdesktop-ek Thunar-en liburutegiak erabiltzen ditu idazmahaieko
aplikazio abiarazle eta fitxategi/karpetak kudeatzeko, txikituriko
leihoen ikonoak bistaratzeko (CDE ezaugarri ezaguna), edo idazmahai
ikonoak guztiz ezgaitzeko gai da ere..</p>

<h2>Mousepad</h2>
<p>Mousepad oinarrizko testu editore bat, Xfce 4.4 zati bezala
banatzen da. Mousepad oso azkar abiarazten da eta oinarrizko testu
edizio ezaugarri ezagunenak ditu, honek Xfce-rentzat ezin hobea
egiten du.</p>
