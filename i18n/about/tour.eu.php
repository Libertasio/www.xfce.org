<h2>Panela</h2>
<p>Xfce Panela guztiz berridatzia izan da Xfce 4.4-erako, eta
panel eta kanpo plugin anitz onartzen ditu (aplikazio bereziak bezala
exekutatzen ditu horren bitartez panelaren egonkortasuna bermatuz).
xftaskbar4 eta xfce4-iconbox banaketatik kanpo geratu dira hauen
ezaugarriak panel berriak hornitzen bait ditu.</p>
<div class="image">
	<a href="/images/about/tour/panel-properties.png">
		<img src="/images/about/tour/panel-properties-thumb.png" border="0" alt="panel properties" />
	</a>
</div>
<p>Panel elementuak orain Eraman'ta'Utzi erabiliaz gehitu edo
ordenatu daitezke, eta panel abiarazle propietate elkarrizketa
berridatzia izan da erantsiriko menuak dituzten abiarazleak hobe
kudeatzeko (Zero Instalaziorako ere onarpena du orain).</p>
<div class="image">
	<a href="/images/about/tour/panel-launcher.png">
		<img src="/images/about/tour/panel-launcher-thumb.png" border="0" alt="panel launcher" />
	</a>
</div>
<p>Panel plugin gehienak, <a href="http://goodies.xfce.org" Xfce
Goodie proiektutik</a> eskura daitezkeenak, panel berrirako
eguneratuak izan dira, eta <code>xfce4-xfapplet-plugin</code> berriak
erabiltzaileari Xfce panelean GNOME panel applet-ak instalatzeko
aukera ematen dio.</p>

<h2>Aplikazio Gogokoenak</h2>
