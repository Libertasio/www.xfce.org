<h2>Orage</h2>
<p>Orage, xfcalendar-en oinarritutako egutegi aplikazio berria, batek
denbora kudeaketa aplikazio bati gau egun eskatzen dizkion ezaugarri
guztiak ditu, nahiz oraindik arin eta erabilerraza izan.</p>
