<h2>Venster beheer afstellen</h2>
<p>Xfwm4, de Xfce venster beheerder heeft veel verbeteringen gezien. Een van de meest interessante zichtbare veranderingen is de toevoeging van een plugin 'Venster beheer afstellen', die de gebruiker de mogelijkheid geeft om geavanceerde opties van de venster beheerder af te stellen.</p>
<div class="image">
	<a href="/images/about/tour/wmtweaks.png">
		<img src="/images/about/tour/wmtweaks-thumb.png" border="0" alt="venster beheerder afstellen" />
	</a>
</div>
<p>Er is ook ondersteuning om de ingebouwde compositie beheerder in de venster beheerder af te stellen, mits <code>xfwm4</code> is gebouwd met ondersteuning voor de Xcomposite extentie.</p>
