<h1>Xfce 4.4 Rondleiding</h1>
<p>Xfce 4.4 is uitgerust met veel nieuwe functies sinds de 4.2 release.
Deze pagina geeft een lijst van de belangrijkste veranderingen in Xfce 4.4. Maar het zal niet gaan over <a href="http://thunar.xfce.org" target="_blank">Thunar</a>.</p>

<h2>Bureaublad iconen</h2>
<p>Een van de meest gevraagde functies in het Xfce 4.0/4.2 tijdperk was de ondersteuning voor bureaublad-iconen. Xfce 4.4 heeft hier ijndelijk ondersteuning voor.</p>
<div class="image">
	<a href="/images/about/tour/desktop-icons.png">
		<img src="/images/about/tour/desktop-icons-thumb.png" border="0" alt="bureaublad iconen" />
	</a>
</div>
<p>Xfdesktop maakt gebruik van Thunar's bibliotheken om programma-starters, bestanden en mappen op het bureaublad weer te geven. Het is ook mogelijk om geminimaliseerde vensters op het bureaublad weer te geven (een populaire CDE functie), of om iconen in zijn geheel uit te schakelen.<p>

<h2>Mousepad</h2>
<p>Mousepad, een simpele en lichtgewicht tekstverwerker, het is geintroduceerd als onderdeel van Xfce 4.4. Mousepad start snel op en is voorzien van alle standaard tekst-bewerkingsfuncties. Dit maakt het een goede standaard tekstverwerker voor Xfce.</p>
<div class="image">
	<a href="/images/about/tour/mousepad.png">
		<img src="/images/about/tour/mousepad-thumb.png" border="0" alt="mousepad" />
	</a>
</div>

<h2>Orage</h2>
<p>Orage, de nieuwe kalender applicatie gebaseerd op xfcalendar, heeft alle functies die je zou verwachten van een tijd-beheer programma vandaag de dag. Terwijl het nog steeds lichtgewicht en gemakkelijk te gebruiken blijft.</p>
<div class="image">
	<a href="/images/about/tour/orage.png">
		<img src="/images/about/tour/orage-thumb.png" border="0" alt="orage" />
	</a>
</div>

<h2>Paneel</h2>
<p>Het Xfce Paneel is volledig herschreven voor 4.4, het ondersteund meerdere panelen en externe plugins (deze draaien als gescheiden programma's waardoor de stabiliteit van het paneel wordt verbeterd). xftaskbar4 en xfce4-iconbox zijn verdwenen omdat hun functionaliteiten nu volledig door het paneel worden ondersteund.</p>
<div class="image">
	<a href="/images/about/tour/panel-properties.png">
		<img src="/images/about/tour/panel-properties-thumb.png" border="0" alt="paneel eigenschappen" />
	</a>
</div>
<p>De paneel plugins kunnen nu worden toegevoegd en verplaatst door deze te slepen, en de eigenschappen van het paneel en de starters zijn opnieuw ontworpen zodat het makkelijker is geweorden om starters te maken en te beheren met bijgevoegde menu's (er is ook nu ondersteuning voor 'Zero Install').</p>
<div class="image">
	<a href="/images/about/tour/panel-launcher.png">
		<img src="/images/about/tour/panel-launcher-thumb.png" border="0" alt="paneel starter" />
	</a>
</div>
<p>De meeste paneel plugins, beschikbaar via het <a href="http://goodies.xfce.org" target="_blank">Xfce Goodies project</a>, zijn ge-update voor het nieuwe paneel, en de nieuwe <code>xfce4-xfapplet-plugin</code> stelt gebruikers in staat GNOME paneel applets toe te voegen aan het Xfce paneel.</p>

<h2>Standaard Programma's</h2>
<p>Een nieuwe toepassing voor het configureren van de standaard programma's zoals een browser, e-mail programma en terminal emulator die gebruikt worden door Xfce programma's. Het is nu niet meer nodig om handmatig shell-profielen aan te passen</p>
