<h1>Xfce特性</h1>
<ul>
  <li>跨平台：Linux、NetBSD、FreeBSD、OpenBSD、Solaris、Cygwin、以及MacOS X</li>
  <li>轻量级桌面环境</li>
  <li>基本的轻量级的应用程序</li>
  <li>最高特性的Goodies</li>
  <li>多显示以及Xinerama模式支持</li>
  <li>Kiosk模式限制配置</li>
  <li>被翻译成40多种语言</li>
</ul>
<ul>
  <li>遵守<a href="http://www.freedesktop.org/" target="_blank" class="external">Freedesktop</a>规范
  <ul>
    <li>Extended Window Manager hints</li>
    <li>桌面菜单</li>
    <li>图标主题</li>
    <li>XDG Base directory</li>
    <li>XSETTINGS协议</li>
    <li>Drag-and-Drop协议</li>
  </ul></li>
</ul>

<p>Xfce提供一个轻量级的桌面环境，拥有默认的图标主题。它使用一个桌面管理器来提供图标，多块面板用于放置默认菜单、常用程序以及其它插件，一个通过Xorg混合特性来实现透明、阴影效果的窗口管理器。它还提供一个会话管理器来实现开关机功能，以及一个可以配置各组件和常规项目的设置管理器。</p>
<p>Xfce提供常用程序，它们和桌面环境一样被设计成轻量级。这些程序是：一个拥有标签功能的现代的终端模拟器、一个基于iCal的具有提醒功能的日历，以及一个控制音量的混音器。许多Xfce程序都拥有一个面板插件。例如日历程序有个可定制日期的插件，混音器有个主音量调节器。</p>
<p>最后Xfce提供一系列终端用户程序，通常设计成轻量级的。这些程序是：一个简单快速的拥有打印支持的文本编辑器、一个易用的文件管理器、一个基于Xine的媒体播放器、一个CD和DVD烧录程序，以及一个支持许多格式的归档管理器。</p>

