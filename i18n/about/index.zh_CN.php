<h1>关于Xfce</h1>

<p>Xfce是运行于类unix系统上的一个轻量级桌面环境。它的设计目标是快速小巧，同时能作到外观华丽且易于使用。</p>
<p>Xfce 4.4体现了传统UNIX哲学中的模块化和可复用性。它包含一系列的组件，这些组件一起提供了一个桌面环境的全部功能。所有组件都被独立打包，你可以自行选择安装各组件来构件一个属于你自己的最佳工作环境。</p>
<p>Xfce 4的另一优点是遵守标准，具体定义请见<a href="http://freedesktop.org" target="_blank">freedesktop.org</a>.</p>
<p>Xfce 4可安装于许多UNIX平台。现已知的平台有Linux, NetBSD, FreeBSD, OpenBSD, Solaris, Cygwin and MacOS X, on x86, PPC, Sparc, Alpha...</p>
