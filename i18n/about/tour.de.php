<h1>Eine Einf&uuml;hrung in Xfce 4.6.0</h1>

<p>
Endlich ist die seit Langem erwartete Version 4.6.0 der 
Arbeitsfl&auml;chenumgebung Xfce erschienen. Wir wollen Ihnen einige neue 
Funktionen zeigen, die seit der letzten stabilen Version hinzugef&uuml;gt 
wurden.
</p>

<h2>Verbesserte Arbeitsfl&auml;che mit Xfdesktop<a name="xfdesktop"></a></h2>

<p>Seit Arbeitsfl&auml;chensymbole in Xfce 4.4 eingef&uuml;hrt wurden, haben 
viele Benutzer den Wunsch ge&auml;u&szlig;rt, mehrere Symbole gleichzeitig 
markieren zu k&ouml;nnen. (Das wird Rubber banding, Gummibandverhalten, 
genannt). Mit <b>Xfce 4.6</b> enth&auml;lt <b>Xfdesktop</b> jetzt diese 
M&ouml;glichkeit: Sie k&ouml;nnen mehrere Symbole gleichzeitig markieren, 
verschieben, entfernen...</p>

<p class="image"><img src="/images/about/tour/4.6/xfdesktop-rubberbanding.png" 
alt="Auswahl von mehreren Symbolen" /></p>

<p>
<b>Xfce 4.6</b> enth&auml;lt auch ein v&ouml;llig neues Anwendungsmen&uuml;, 
das Ihnen nicht nur erm&ouml;glicht, Dateien wie mit dem Kontextmen&uuml; von 
<b>Thunar</b> zu bearbeiten, sondern auch Anwendungen zu &ouml;ffnen, Ihre 
Sitzung zu beenden oder die Hilfe auszurufen.
</p>

<p class="image"><img src="/images/about/tour/4.6/xfdesktop-menu.png" 
alt="Neues Anwendungsmen&uuml;" /></p>

<h2>Leiste<a name="panel"></a></h2>

<p>Eine Reihe von Programmfehlern in der <b>Leiste</b> ist behoben worden, 
insbesondere f&uuml;r die Verwendung mehrere Bildschirme. Diese Version 
enth&auml;lt au&szlig;erdem eine erweiterte Auswahl an Erweiterungen f&uuml;r 
die Leiste.
</p>

<p class="image"><img src="/images/about/tour/4.6/xfce4-panel-clock.png" 
alt="Neue Bin&auml;ruhr" /></p>

<p>
Die <b>Uhr</b> f&uuml;r die Leiste wurde neu geschrieben, damit weniger 
Resourcen verbraucht werden und um einige Anzeigefehler zu beheben. 
Zus&auml;tzlich gibt es auch noch eine Uhr f&uuml;r den Geek in Ihnen: 
Die Bin&auml;ruhr! Das &uuml;berarbeitete <b>Benachrichtigungsfeld</b> erlaubt 
Ihnen, Hinweiszeichen, die sie nicht sehen m&ouml;chten, auszublenden und so 
das Feld aufger&auml;umt und &uuml;bersichtlich zu halten.
</p>

<h2>Neue Laust&auml;rkeregelung<a name="sound-mixer"></a></h2>

<p>
Die <b>Lautst&auml;rkeregelung</b> wurde v&ouml;llig neu geschrieben und 
verwendet jetzt <a href="http://www.gstreamer.net/">Gstreamer</a>, das die 
Verwendung von mehreren Soundsystemen erlaubt. Die Bedienoberfl&auml;che wurde 
aufpoliert, Sie k&ouml;nnen mehrere Soundkarten verwalten und eine Erweiterung 
f&uuml;r die Leiste erlaubt Ihnen, die Systemlautst&auml;rke mit dem Mausrad 
einzustellen.
</p>

<p class="image"><img src="/images/about/tour/4.6/xfce4-mixer.png" 
alt="Neue Lautst&auml;rkeregelung" /></p>

<h2>Verbesserte Sitzungsverwaltung<a name="session-manager"></a></h2>

<p>
<b>Xfce 4.6</b> enth&auml;lt eine weiter entwickelten Sitzungsverwaltung: Ihre 
Sitzung sollte schneller starten und der Einstellungsdialog wurde 
&uuml;berarbeitet, um Anwendungen, die mit der Xfce-Sitzung zusammenarbeiten, 
besser verwalten zu k&ouml;nnen. Weiter &uuml;berwacht die Sitzungsverwaltung 
jetzt f&uuml;r die Sitzung wichtige Programme und startet sie gegebenenfalls 
neu, sodass Sie sich nie mehr ohne Arbeitsfl&auml;che, Leiste, 
Fensterverwaltung, etc. vorfinden werden, falls diese abst&uuml;rzen.
</p>

<p class="image"><img src="/images/about/tour/4.6/xfce4-session-settings.png" 
alt="Einstellungsdialog der Sitzungsverwaltung" /></p>

<p>
Sie enth&auml;lt auch eine Funktion, die lange auf der Wunschliste stand: Von
Haus aus kann man nun den Computer neu starten oder herunterfahren. Und ohne 
Zusatzprogramme ist es jetzt m&ouml;glich, Ihren Computer in den 
<b>Bereitschaftsmodus</b> oder <b>Ruhezustand</b> zu versetzen. Der 
Abmeldedialog wurde mit zwei entsprechenden Schaltfl&auml;chen ausgestattet.
</p>

<p class="image"><img src="/images/about/tour/4.6/xfce4-session-logout.png" 
alt="Abmeldedialog" /></p>

<h2>Fensterverwaltung<a name="window-manager"></a></h2>

<p>
Wie &uuml;blich wurde <b>Xfwm4</b> seit der letzten Version aufw&auml;ndig 
weiterentwickelt: Viele Fehler sind behoben worden, die Unterst&uuml;tzung 
f&uuml;r mehrere Bildschirme wurde verbessert und die Effizienz wurde 
erh&ouml;ht. Einige nette Funktionen wurden zu <b>Xfwm4</b> hinzugef&uuml;gt.
</p>

<p><b>Xfwm4</b> kann nun selbstst&auml;ndig Fenster erkennen, die nicht mehr 
reagieren und Ihnen anbieten, diese zu beenden.</p>

<p class="image"><img src="/images/about/tour/4.6/xfwm4-net-ping.png" 
alt="Dialog zum Beenden eingefrorener Anwendungen" /></p>

<p>
Es gibt jetzt auch ein neues <b>Aktionsmen&uuml;</b>, mit dem Sie schnell und 
einfach Fenster verschieben, ihre Gr&ouml;&szlig;e ver&auml;ndern, sie &uuml;ber 
oder unter andere Fenster platzieren oder sie maximieren k&ouml;nnen.
</p>

<p class="image"><img src="/images/about/tour/4.6/xfwm4-new-menu.png" 
alt="Das neue Aktionsmen&uuml;" /></p>

<p>
Die Funktion &raquo;<b>F&uuml;llen</b>&laquo; wurde entwickelt; Sie 
vergr&ouml;&szlig;ert ein Fenster auf die verf&uuml;gbare Gr&ouml;&szlig;e, ohne 
benachbarte Fenster zu &uuml;berlappen. 
</p>

<p class="image"><img src="/images/about/tour/4.6/xfwm4-fill-operation.png" 
alt="Die Funktion &raquo;F&uuml;llen&laquo;" /></p>

<p>Das Flackern, das manchmal entsteht, wenn man Fenster in der Gr&ouml;&szlig;e 
ver&auml;ndert oder verschiebt, wurde deutlich reduziert.</p>

<p class="image"><img src="/images/about/tour/4.6/xfwm4-resize.png" 
alt="flackerfreies Vergr&ouml;&szlig;ern" /></p>

<p>
Au&szlig;erdem wurden einige <b>Einstellungsm&ouml;glichkeiten</b> 
hinzugef&uuml;gt: Sie k&ouml;nnen z.B. abstellen, dass Fenster blinken, wenn 
diese ein Dringlichkeitssignal  erhalten.
</p>

<p class="image"><img src="/images/about/tour/4.6/xfwm4-new-tweaks.png" 
alt="neue Einstellungsm&ouml;glichkeiten" /></p>

<h2>Dateimanager<a name="file-manager"></a></h2>

<p>
Es gibt eine Reihe von Verbesserungen und Effizienssteigerungen in 
<b>Thunar</b>. Er kann jetzt - falls vorhanden - die Vorw&auml;rts- und 
R&uuml;ckw&auml;rtstasten Ihrer Maus benutzen, um zu navigieren. 
Au&szlig;erdem gibt es jetzt eine Erweiterung, mit der Sie ein Hintergrundbild 
einrichten k&ouml;nnen.
</p>

<p class="image"><img src="/images/about/tour/4.6/thunar-wallpaper-plugin.png" 
alt="Ein Bild aus einem Thunarverzeichnis als Hintergrundbild einrichten" /></p>

<p><b>Thunar</b> befolgt jetzt die Spezifikationen f&uuml;r 
<a href="http://freedesktop.org/wiki/Software/xdg-user-dirs">
Benutzerverzeichnisse nach XDG</a>, die Ihnen erm&ouml;glicht, Motive und 
Spracheinstellungen f&uuml;r Ihre pers&ouml;nlichen Verzeichnisse, z.B. 
f&uuml;r Musik, Dokumente, Videos, Vorlagen etc. zu verwenden.
</p>

<p class="image"><img src="/images/about/tour/4.6/thunar-xdg-user-dirs.png" 
alt="Thunars Men&uuml; f&uuml;r Benutzerverzeichnisse" /></p>

<p>
<b>Thunar</b> zeigt die nicht eingeh&auml;ngten Ger&auml;te und Verzeichnisse 
transparent an, damit Sie diese von den eingeh&auml;ngten unterscheiden 
k&ouml;nnen.
</p>

<p class="image"><img src="/images/about/tour/4.6/thunar-mounting.png" 
alt="Transparante Symbole f&uuml;r nicht eingeh&auml;ngte Ger&auml;te und 
Verzeichnisse" /></p>

<p>
Zu guter Letzt unterst&uuml;tzt <b>Thunar</b> jetzt auch verschl&uuml;sselte 
Ger&auml;te!
</p>

<p class="image"><img src="/images/about/tour/4.6/thunar-encrypted.png" 
alt="Unterst&uuml;tzung f&uuml;r verschl&uuml;sselte Ger&auml;te in Thunar" /></p>

<h2>Neue Einstellungsdialoge<a name="configuration-dialogs"></a></h2>

<p>
Xfce 4.6 enth&auml;lt eine neue Einstellungsoberfl&auml;che, den <b>Xfce 
Einstellungsmanager</b>, der Ihnen erm&ouml;glicht, Ihre 
Arbeitsoberfl&auml;che einfacher einzurichten: Die Dialoge, die Sie durch einen 
einfachen Klick auf die Schaltfl&auml;chen erreichen k&ouml;nnen, wurden 
&uuml;berarbeitet. Sie sind jetzt kompakter und stehen Ihnen schneller und 
intuitiver bei der Einrichtung Ihrer Arbeitsoberfl&auml;che zur Seite.
</p>

<p class="image"><img src="/images/about/tour/4.6/xfce4-settings-manager.png" 
alt="Xfce4 Einstellungsmanager" /></p>

<h3>Barrierefreiheitseinstellungen<a name="accessibility"></a></h3>
<p class="image"><img src="/images/about/tour/4.6/xfce4-accessibility-settings.png" 
alt="Barierrefreiheitseinstellungsdialog" /></p>

<p>
Der Dialog &raquo;<b>Barierrefreiheitseinstellungen</b>&laquo; erlaubt Ihnen, 
die Barrierefreiheit Ihres Computers in Bezug auf Maus- und 
Tastatureinstellungen einzustellen, so z.B. klebrige oder h&uuml;pfende Tasten 
oder eine Tastaturmaus.
</p>

<h3>Darstellungseinstellungen<a name="appearance"></a></h3>
<p class="image"><img src="/images/about/tour/4.6/xfce4-appearance-settings.png" 
alt="Darstellungseinstellungsdialog" /></p>

<p>
Der Dialog &raquo;<b>Darstellungseinstellungen</b>&laquo; erlaubt Ihnen, den 
Stil f&uuml;r die Bedienelemente, das verwendete Symbolthema, Schrifteinstellungen, 
Werkzeugleisten und Men&uuml;optionen einzurichten. 
</p>

<h3>Anzeige<a name="display"></a></h3>
<p class="image"><img src="/images/about/tour/4.6/xfce4-display-settings.png" 
alt="Anzeigendialog" /></p>

<p>Der Dialog &raquo;<b>Anzeigene</b>&laquo; erlaubt Ihnen, die 
Bildschirmaufl&ouml;sung, die Wiederholungsrate und die Rotation f&uuml;r jeden 
angeschlossenen Bildschirm einzustellen.

<h3>Tastatureinstellungen<a name="keyboard"></a></h3>
<p class="image"><img src="/images/about/tour/4.6/xfce4-keyboard-settings-layout.png" 
alt="Tastatureinstellungsdialog, Reiter &raquo;Tastaturbelegung&laquo;" /></p>

<p>Der Dialog &raquo;<b>Tastatureinstellungen</b>&laquo; erlaubt Ihnen, 
Einstellungen wie die Wiederholungsrate, Tastenkombinationen und Ihre 
Tastaturbelegung zu ver&auml;ndern.
</p>

<p class="image"><img src="/images/about/tour/4.6/xfce4-keyboard-settings-shortcuts.png" 
alt="Tastatureinstellungsdialog, Reiter &raquo;Tastenkombinationen&laquo;" /></p>

<p>
Sie k&ouml;nnen jetzt Tastenkombinationen einfacher eingeben und Konflikte 
werden automatisch erkannt.
</p>

<h3>Mauseinstellungen<a name="mouse"></a></h3>
<p class="image"><img src="/images/about/tour/4.6/xfce4-mouse-settings.png" 
alt="Mauseinstellungsdialog" /></p>

<p>
Der Dialog &raquo;<b>Mauseinstellungen</b>&laquo; erlaubt Ihnen, verschiedene 
Mausger&auml;te, die an Ihren Computer angeschlossen sind, zu verwalten. Richten 
Sie die Maustastenbelegung, die Zeigergeschwindigkeit, die Doppelklickgeschwindigkeit, 
den Zeigerstil etc. ein.
</p>

<h3>Arbeitsfl&auml;cheneinstellungen<a name="desktop-settings"></a></h3>

<p class="image"><img src="/images/about/tour/4.6/xfdesktop-settings.png" 
alt="Arbeitsfl&auml;cheneinstellungsdialog" /></p>

<p>
Der Dialog &raquo;<b>Arbeitsfl&auml;cheneinstellungen</b>&laquo; ist jetzt viel 
kompakter, er erlaubt Ihnen, Einstellungen f&uuml;r jeden Bildschirm 
vorzunehmen: Hintergrundbild, Helligkeit, Arbeitsfl&auml;chenmen&uuml;, 
angezeigte Symbole, ...</p>

<h2>Neuer Anwendungsfinder<a name="appfinder"></a></h2>

<p>
<b>Xfce 4.6</b> enth&auml;lt jetzt einen v&ouml;llig neuen Anwendungsfinder, 
der eine aufger&auml;umte Bedienoberfl&auml;che f&uuml;r Sie bereit h&auml;lt. 
Er ist einfacher mit der Tastatur zu steuern und &uuml;berwacht die Installation 
von neuen Programmen, sodass die Liste immer aktuell ist. Es ist au&szlig;erdem 
m&ouml;glich, schnell Starter in der Leiste zu erstellen, indem Sie einfach ein 
Anwendungssymbol in das Fenster zum Erstellen von Startern ziehen.
</p>

<p class="image"><img src="/images/about/tour/4.6/xfce4-appfinder.png" 
alt="Neuer Anwendungsfinder" /></p>

