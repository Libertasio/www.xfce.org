<h1>Características do Xfce</h1>
<ul>
  <li>Multiplataforma: Linux, NetBSD, FreeBSD, OpenBSD, Solaris, Cygwin e MacOS X</li>
  <li>Ambiente de desktop leve e rápido</li>
  <li>Oferece aplicações básicas e leves</li>
  <li>Goodies para uma personalização máxima<li>
  <li>Suporta diversos monitores e Xinerama</li>
  <li>Disponibiliza um modo kiosk para restringir as configurações do Xfce</li>
  <li>Traduzido em mais de 40 idiomas</li>
</ul>
<ul>
  <li>Implementado de acordo com as especificações do <a href="http://www.freedesktop.org/" target="_blank" class="external">Freedesktop</a>
  <ul>
    <li>Gestor de janelas com suporte a sugestões extendidas (hints)</li>
    <li>Especificação para construção de menus no desktop</li>
    <li>Especificação de temas para os ícones</li>
    <li>Especificação da localização base para pastas XDG</li>
    <li>Suporte ao protocolo XSETTINGS</li>
    <li>Suporte ao protocolo Drag-and-Drop</li>
  </ul></li>
</ul>

<p>O ambiente de trabalho do Xfce fornece uma interface gráfica leve e rápida e ao mesmo tempo estável e atraente. O Xfce disponibiliza ícones e temas padrões, um gestor de desktop com suporte a ícones na área de trabalho e também suporte a múltiplos painéis para adição de menus e lançadores, preferência de aplicações e plugins adicionais. O Xfce também dispõe de um gestor de sessão que garante que o desktop vai estar como o deixou da próxima vez que o usar.
O gestor de configurações do Xfce é formado por um painel que permite ajustar o comportamento e a aparência de várias aplicações de forma conveniente, além de ajustes estéticos, também podem ser realizados ajustes de som, impressão e data/hora. O gestor de janelas do Xfce também já possui suporte ao recurso de composição do Xorg para a realização de efeitos de transparência e sombra.</p>

<p>O Xfce também fornece aplicações que são projectadas para serem leves e rápidas para o ambiente de desktop. Algumas destas aplicações são: um moderno emulador de terminal com separadores, um calendário baseado no iCal para notificação de eventos e agendamentos e um controlador de nível de volume. Algumas aplicações Xfce também disponibilizam um plugin para o painel. Por exemplo: o calendário oferece um plugin para mostrar a data personalizada e o controlador de nível de volume oferece um controlo do volume principal.</p>

<p>Finalmente, o Xfce fornece aplicações para os utilizadores finais, sempre à procura da leveza. Algumas das aplicacões são: um leve e simples editor de textos com suporte a impressão, um gestor de ficheiros de fácil utilização, um leitor multimédia baseado na biblioteca do Xine, um gravador de CD e DVD e um gestor de compressão de arquivos com suporte a múltiplos formatos de compressão.</p>
