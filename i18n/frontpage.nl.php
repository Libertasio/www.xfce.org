	<div id="front-intro-about">
		<h1>Over Xfce</h1>
		<p>Xfce is een lichte desktop omgeving voor verschillende *NIX systemen. Omdat het ontworpen is om snel mee te kunnen werken, laadt het snel de programma's, terwijl het niet het geheugen vol propt.</p>
		<p>Xfce 4,2 neemt de traditionele filosofie van Unix van modulariteit en mogelijkheid om opnieuw gebruikt te worden op. Het bestaat uit een aantal componenten die samen de volledige functionaliteit van het Desktopmilieu verstrekken. Zij worden afzonderlijk verpakt en u kunt en van de beschikbare pakketten verkiezen om de beste persoonlijke werkomgeving plukken tot stand te brengen.</p>
