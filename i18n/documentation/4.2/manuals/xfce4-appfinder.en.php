  <h2><a name=
  "appfinder-advanced"></a>Appfinder Advanced features</h2>

  <p>The Xfce 4 appfinder has the possibility to interact with
  other applications through drag and drop. At the actual state it
  can drop things on Xfce 4 menu editor and on some parts of Xfce 4
  panel.</p>

  <p>You can edit desktop menu through Xfce 4 menu editor using
  drag and drop from Xfce 4 Appfinder. Searching applications and
  adding it to desktop menu will be easier to do and faster. You
  have only to select an item from the appfinder list and drag it
  on the menu editor list and enjoy the result.</p>

  <p>Xfce 4 appfinder also support drag and drop on Xfce 4 panel
  launcher settings window. You can drop appfinder items on the
  Command text entry of Xfce 4 panel launcher settings and get the
  full command written. A fast way to add a new application.</p>

  

