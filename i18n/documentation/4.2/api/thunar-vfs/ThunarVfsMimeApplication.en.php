<div class="refsect2" lang="en">
<a name="id2631601"></a><h3>
<a name="thunar-vfs-mime-application-new-from-file"></a>thunar_vfs_mime_application_new_from_file ()</h3>
<a class="indexterm" name="id2631681"></a><pre class="programlisting"><a href="ThunarVfsMimeApplication.html" title="ThunarVfsMimeApplication">ThunarVfsMimeApplication</a>* thunar_vfs_mime_application_new_from_file
                                            (const gchar *path,
                                             const gchar *desktop_id);</pre>
<p>
Generates a new <a href="ThunarVfsMimeApplication.html" title="ThunarVfsMimeApplication"><span class="type">ThunarVfsMimeApplication</span></a> for the application
described by <em class="parameter"><code>path</code></em> and <em class="parameter"><code>desktop_id</code></em>.
</p>
<p>
The caller is responsible to free the returned instance using
<code class="function">g_object_unref()</code> when no longer needed.
</p>
<p>
You should really seldomly use this function and always
prefer <a href="ThunarVfsMimeApplication.html#thunar-vfs-mime-application-new-from-desktop-id"><code class="function">thunar_vfs_mime_application_new_from_desktop_id()</code></a>.</p>
<p>

</p>
<div class="variablelist"><table border="0">
<col align="left" valign="top">
<tbody>
<tr>
<td>
<span class="term"><em class="parameter"><code>path</code></em>&#160;:</span></td>
<td> the absolute path to the desktop file.
</td>
</tr>
<tr>
<td>
<span class="term"><em class="parameter"><code>desktop_id</code></em>&#160;:</span></td>
<td> the desktop-id of the file.
</td>
</tr>
<tr>
<td>
<span class="term"><span class="emphasis"><em>Returns</em></span>&#160;:</span></td>
<td> the <a href="ThunarVfsMimeApplication.html" title="ThunarVfsMimeApplication"><span class="type">ThunarVfsMimeApplication</span></a> for <em class="parameter"><code>desktop_id</code></em>
              or <code class="literal">NULL</code>.
</td>
</tr>
</tbody>
</table></div>
