<div class="refsect2" lang="en">
<a name="id2631480"></a><h3>
<a name="thunar-vfs-mime-application-new-from-desktop-id"></a>thunar_vfs_mime_application_new_from_desktop_id ()</h3>
<a class="indexterm" name="id2631492"></a><pre class="programlisting"><a href="ThunarVfsMimeApplication.html" title="ThunarVfsMimeApplication">ThunarVfsMimeApplication</a>* thunar_vfs_mime_application_new_from_desktop_id
                                            (const gchar *desktop_id);</pre>
<p>
Generates a <a href="ThunarVfsMimeApplication.html" title="ThunarVfsMimeApplication"><span class="type">ThunarVfsMimeApplication</span></a> instance for the application
referenced by <em class="parameter"><code>desktop_id</code></em>. Returns <code class="literal">NULL</code> if <em class="parameter"><code>desktop_id</code></em> is not valid.
</p>
<p>
The caller is responsible to free the returned instance using
<code class="function">g_object_unref()</code> when no longer needed.</p>
<p>

</p>
<div class="variablelist"><table border="0">
<col align="left" valign="top">
<tbody>
<tr>
<td>
<span class="term"><em class="parameter"><code>desktop_id</code></em>&#160;:</span></td>
<td> the id of an application's .desktop file.
</td>
</tr>
<tr>
<td>
<span class="term"><span class="emphasis"><em>Returns</em></span>&#160;:</span></td>
<td> the <a href="ThunarVfsMimeApplication.html" title="ThunarVfsMimeApplication"><span class="type">ThunarVfsMimeApplication</span></a> for <em class="parameter"><code>desktop_id</code></em> or <code class="literal">NULL</code>.
</td>
</tr>
</tbody>
</table></div>
</div>
