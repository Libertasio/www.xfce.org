</p></div><div class="refsect1" lang="en"><a name="id2549555"></a><h2>Details</h2><div class="refsect2" lang="en"><a name="id2549561"></a><h3><a name="xfce-utf8-remove-controls"></a>xfce_utf8_remove_controls ()</h3><a class="indexterm" name="id2549572"></a><pre class="programlisting">gchar*      xfce_utf8_remove_controls       (gchar *str,
                                             gssize max_len,
                                             const gchar *end);</pre><p>
Removes all control characters from <i class="parameter"><tt>str</tt></i> up to <i class="parameter"><tt>end</tt></i> or up to
<i class="parameter"><tt>max_len</tt></i> characters (note that characters does not mean bytes with
UTF-8), where both <i class="parameter"><tt>str</tt></i> and <i class="parameter"><tt>max_len</tt></i> may not be given.
</p><p>
Control characters are replaced in <i class="parameter"><tt>str</tt></i> by whitespaces, no new string
will be allocated. The operation is done in-place.</p><p>

</p><div class="variablelist"><table border="0"><col align="left" valign="top"><tbody><tr><td><span class="term"><i class="parameter"><tt>str</tt></i> :</span></td><td> target string.
</td></tr><tr><td><span class="term"><i class="parameter"><tt>max_len</tt></i> :</span></td><td> max characters to check or -1 for no character limit.
</td></tr><tr><td><span class="term"><i class="parameter"><tt>end</tt></i> :</span></td><td> pointer to the endpoint in <i class="parameter"><tt>str</tt></i> or <tt class="literal">NULL</tt> for no endpoint.
</td></tr><tr><td><span class="term"><span class="emphasis"><em>Returns</em></span> :</span></td><td> pointer to <i class="parameter"><tt>str</tt></i> or <tt class="literal">NULL</tt> on error.

</td></tr></tbody></table></div><p>Since  4.2
