<a name="id2639846"></a><h3>
<a name="thunar-vfs-volume-get-mount-point"></a>thunar_vfs_volume_get_mount_point ()</h3>
<a class="indexterm" name="id2639858"></a><pre class="programlisting"><a href="thunar-vfs-ThunarVfsPath.html#ThunarVfsPath">ThunarVfsPath</a>* thunar_vfs_volume_get_mount_point
                                            (<a href="ThunarVfsVolume.html" title="ThunarVfsVolume">ThunarVfsVolume</a> *volume);</pre>
<p>
Determines the current mount point for <em class="parameter"><code>volume</code></em>. If <em class="parameter"><code>volume</code></em>
is mounted this will be the location at which it is currently
mounted. Else it will be the location where <em class="parameter"><code>volume</code></em> is most
probably being mounted. Note that this location may change
during a call to <a href="ThunarVfsVolume.html#thunar-vfs-volume-mount"><code class="function">thunar_vfs_volume_mount()</code></a>, so be sure to
check the mount point after the call to <a href="ThunarVfsVolume.html#thunar-vfs-volume-mount"><code class="function">thunar_vfs_volume_mount()</code></a>.</p>
