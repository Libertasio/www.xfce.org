  <h2 style="clear: both"><a name="xfce4-intro" id=
  "xfce4-intro"></a>Introduction</h2>

  <p>Xfce is a lightweight desktop environment for unix-like
  operating systems. It aims to be fast and light on system
  resources, while still being visually appealing and easy to
  use.</p>

  <p>Xfce 4 embodies the traditional UNIX philosophy of modularity
  and re-usability. All of Xfce 4's core components can be build
  and installed separately. Xfce 4 is based on the <a href=
  "http://www.gtk.org" target="_top">GTK+ toolkit</a> version
  2.</p>

  <p>Adhereance to standards is one of the main design goals of
  Xfce 4, specifically those defined at <a href=
  "http://www.freedesktop.org" target="_top">Freedesktop.org</a>.
  This allows Xfce 4 to seamlessly interoperate with programs
  written for other desktop environments that follow these
  specifications.</p>

  <p>Xfce is all free software. The separate components are
  released under either the BSD license, the GNU LGPL or the GNU
  GPL (see <a href="xfce4-copyright.html" title=
  "Copyright and Licences">the section called “Copyright and
  Licences”</a> for details).</p>

