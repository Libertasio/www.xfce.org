<h2 style="clear: both"><a name="preface" id=
"preface"></a>Preface</h2>

<p>This document gives an overview of the XFce 4 Desktop
Environment and explains how to perform several common tasks. It
is meant to be a starting point for anyone who wants to use XFce
4 and it includes pointers to other sources of information.</p>

<p>The latest version of this document can always be found at
this location: <a href=
"http://members.home.nl/xfce4/documentation/xfce4-user-guide.html"
target=
"_top">http://www.xfce.org/documentation/xfce4-user-guide.html</a>.</p>


