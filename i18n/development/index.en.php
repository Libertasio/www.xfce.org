<h2>Development</h2>

<p>
Xfce is only made possible with the help of volunteers with all kinds 
of skills and talent: coding is of course one of the main ways of helping, but
translators, documentators or designers are also welcome!
</p>

<h3 class="dev" style="background-image: url(/images/development/devel.png);">Contribute to the development of the Xfce core</h3>

<p>
Feel free to join us developing the next major branch of Xfce, or fix bugs in existing releases. The
<a href="http://bugzilla.xfce.org">bugzilla</a> and the <a href="http://wiki.xfce.org/releng/4.8/schedule">roadmap</a>
are a good start to get ideas on what needs some love.
</p>

<p>
If you need more details or want to get news about the current development, suscribe the
<a href="http://foo-projects.org/mailman/listinfo/xfce4-dev">development mailing list</a>.
The <a href="http://wiki.xfce.org/">Xfce wiki</a> also contains a lot of additionnal information.
</p>

<h3 class="dev" style="background-image: url(/images/development/goodies.png);">Goodies</h3>

<p>
The <a href="http://goodies.xfce.org">Xfce goodies</a> are independant projects related to the
Xfce desktop, but they are not part of the official release. They also need your help: you should
contact the maintainer of the project you want to contribute to, but you can also start your own project. 
The <a href="http://foo-projects.org/mailman/listinfo/goodies-dev/">Goodies development mailing list</a>
is there for you if you have any questions.
</p>

<h3 class="dev" style="background-image: url(/images/development/i18n.png);">Translations</h3>

<p>
Translations are currently managed using <a href="http://wiki.xfce.org/translations/transifex">Transifex</a>.
The <a href="http://i18n.xfce.org/">Xfce i18n wiki</a> contains all instructions on how to contribute 
to the translations. If you have any questions, the 
<a href="http://foo-projects.org/mailman/listinfo/xfce-i18n/">Xfce i18n mailing list</a>
is here for you!
</p>

<h3 class="dev" style="background-image: url(/images/development/docs.png);">Documentation</h3>

<p>
As you may have noticed, the <a href="/documentation/">Xfce documentation</a> needs a lot of work.
A new documentation is being worked on, if you want to join this new effort, please contact us using the
<a href="http://foo-projects.org/mailman/listinfo/xfce4-dev">development mailing list</a>.
