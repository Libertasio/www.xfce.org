<h1>Translating the Xfce Web site</h1>

<p>
The Xfce website is designed to be easily translatable into different languages,
so that the content is accessible to people who do not speak English.
</p>

<p>
Our goal is to make the Xfce website available in as many different languages 
as possible. To achieve this, we need your help to translate the different 
webpages into new languages and to improve the existing translations.
</p>

<p>
The <a href="http://i18n.xfce.org/" title="i18n">Xfce i18n project</a>
manages the translation of the website, as well as the one of the documentation and
of the applications of the Xfce desktop environment.
</p>
