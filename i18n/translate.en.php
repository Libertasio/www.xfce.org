<h1>Translating Xfce webpages</h1>

<p>The Xfce website is designed to be translated into different languages 
easily, so that the content can be accessible to non-english speaking users 
and an easy way.</p>

<p>Our goal is to make the Xfce website available in as many different languages 
as possible. To achieve this, we need you to help out translating the different 
webpages into new languages.</p>

<p>The <a href="http://i18n.xfce.org/" title="i18n">i18n Xfce sub-project</a> 
is the place to be to help out with translations. The i18n project covers all 
translation components - including this website, programs and documentation.</p>
