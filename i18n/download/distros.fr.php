<h2>Zenwalk</h2>
<table border="0" cellspacing="10" cellpadding="0">
	<tr>
		<td><a href="http://www.zenwalk.org/" target="_blank" title="Visitez le site Web de Zenwalk"><img src="/images/download/distros/zenwalk.png" alt="Zenwalk" border="0"/></a></td>
		<td>Zenwalk est un système basé sur Slackware. Le but de Zenwalk est d'offrir un environnement léger et rapide avec une application par tâche, le tout combiné avec le bureau Xfce. Zenwalk contient les dernières technologies Linux, un environnement de programmation complet ainsi que les librairies pour assurer une plateforme de programmation complète idéale pour tout programmeur d'applications. L'approche modulaire du système Zenwalk permet de l'adapter à souhait pour en faire un serveur moderne (e.g. LAMP, messagerie, partage de fichiers...).</td>
