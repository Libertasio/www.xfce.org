<h2>Zenwalk</h2>
<table border="0" cellspacing="10" cellpadding="0">
	<tr>
		<td><a href="http://www.zenwalk.org/" target="_blank" title="Besuchen Sie die Zenwalk-Website"><img src="/images/download/distros/zenwalk.png" alt="Zenwalk" border="0"/></a></td>
		<td>Zenwalk ist ein Betriebssystem, das auf Slackware basiert. Ziel von Zenwalk ist es, schlank und schnell zu bleiben, indem es nur eine Anwendung pro Aufgabe zur Verfügung stellt und die Xfce-Arbeitsumgebung verwendet. 
		Zenwalk ist mit der neuesten Linux-Technologie ausgestattet und bietet zusätzlich eine vollständige Programmierumgebung mit allen notwendigen Bibliotheken und wird damit zur perfekten Plattform für Anwendungsprogrammierer. 
		Zenwalks modularer Ansatz macht es außerdem noch besonders einfach Zenwalk-Linux in einen sauber aufgesetzten modernen Server zu verwandeln (z.B.: LAMP, Messaging, File-Sharing).</td>
	</tr>
