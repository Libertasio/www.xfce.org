<h1>Xfceのウェブサイトを翻訳する</h1>

<p>Xfceのウェブサイトは、コンテンツが英語が話ない人にも簡単な方法でアクセスできるようにするために、
容易に他の言語に翻訳できるように設計されています。

<p>私達の目的は、できるだけ沢山の言語でXfceのウェブサイトを利用できるようにすることです。
これを達成するためには、あなたがウェブページを新しい言語への翻訳し一役買ってくれることが必要なのです。

<p><a href="http://i18n.xfce.org/" title="i18n">i18n Xfce sub-project</a>が翻訳を手伝うための場所になっています。
i18nプロジェクトは全ての翻訳物（このウェブサイト、プログラム、説明書）を対象としています。 </p>
