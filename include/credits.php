function credits_core_developers ($credits_email, $credits_i18n)
{
  $at = _at ();
  foreach ($credits_email as $v)
    {
      $color = ($color + 1) % 2;
      $bgcolor = ($color) ? ' bgcolor="#eeeeee"' : '';
      echo <<<EOF
    <tr>
        <td width="40%"$bgcolor>{$v[0]} [{$v[1]}$at{$v[2]}]</td>
        <td width="60%"$bgcolor>{$credits_i18n[$v[1]]}</td>
    </tr>
EOF;
    }
}

